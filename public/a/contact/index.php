        <h3>What is the nature of your enquiry?</h3>
        <h4>I've lost my license code</h4>
        <p>
          You can have your license code resent to you by using the
          <a href="https://wincent.com/a/support/registration/"
            >registration retrieval system</a
          >. Simply enter your registered email address and click the button to
          have your registration details resent to you immediately by the
          automated system. As this there is no human intervention this is the
          quickest way to get your license code resent to you. Remember to
          always keep reliable backups of your registration notices; it is a
          good idea to not only burn a copy to CD or DVD, but to keep a printed
          hard copy as well.
        </p>
        <p class="more-categories">
          Click
          <a href="https://wincent.com/a/support/registration/">here</a> to use
          the registration retrieval system
        </p>
        <h4>I have a feature request</h4>
        <p>
          The best way to make a feature request is to use the
          bug tracking and feature request database. If you have an idea for a great new feature then it's probable that
          other users will want the same thing too. By using the database you
          can see what other people have asked for and what is already being
          worked on. By adding comments to or voting for an existing feature
          request, you help Wincent prioritize features according to popularity
          and demand. If you're the first person to request a particular
          feature, be sure to begin your summary with the word "REQUEST" so that
          it stands out. Wincent receives a copy by email of every feature
          request, comment and vote that gets filed using the database; using
          the database is the best way to get your desired feature implemented.
          The database runs the industry-standard
          <a href="http://bugzilla.org/">Bugzilla</a>.
        </p>
        <h4>I want to report a bug</h4>
        <p>
          Just like your feature requests, your bug report will be most
          effective if you lodge it using the
          bug tracking and feature request database. In many cases the bugs you find will depend on specific
          characteristics of your machine or its configuration; often Wincent
          cannot reproduce the bug here. By using the database to share
          information with other users, the common underlying triggers can be
          identified and the problem can be fixed. You can increase the priority
          of an issue by adding comments to or voting for an existing bug
          report. Wincent receives a copy by email of every bug report, comment
          and vote that gets filed using the database, so using the database is
          the quickest way to get your issue fixed. The database runs the
          industry-standard <a href="http://bugzilla.org/">Bugzilla</a>.
        </p>
        <h4>I need technical support</h4>
        <p>
          There are a number of support resources available to you on this
          website 24 hours per day, 365 days per year. You can access them by
          doing the following:
        </p>
        <ul>
          <li>
            Check out the site's
            <a href="https://wincent.com/a/support/faq/"
              >Frequently Asked Questions</a
            >
          </li>
          <li>
            Look at the documentation for a specific
            <a href="https://wincent.com/a/products/">product</a>: every Wincent
            product has a Frequently Asked Questions page as well as detailed
            documentation.
          </li>
          <li>
            Visit the <a href="https://wincent.com/a/news/">news page</a> to see
            if there are any known issues that have recently been discovered
          </li>
          <li>
            Search the
            bug tracking database to see
            if your issue is already under investigation
          </li>
          <li>
            Explore the
            <a href="https://wincent.com/a/knowledge-base/">knowledge base</a>
            for general hints and tips
          </li>
        </ul>
        <p>
          Using these resources is often the quickest way to find a solution for
          your technical problem. If the online resources don't provide you with
          the solution, then you are welcome to
          <a href="https://wincent.com/a/support/tickets/"
            >open a support ticket</a
          >. Wincent is a very small enterprise (only one person) and so the
          quickest way to obtain an answer is usually to access the online
          resources. I work seven days a week and try very hard to attend to
          support tickets as quickly as possible, but there may be some delay,
          depending on the the time of day (and your timezone compared to mine),
          the day of the week, and my level of access to the Net at a given
          moment. If I have to leave town and anticipate not having access to
          the net I always post a notice on the
          <a href="https://wincent.com/a/news/">news page</a>.
        </p>
        <h4>I want to change my email address</h4>
        <p>
          If you want to update your mailing list subscriptions, please see the
          <a href="https://wincent.com/a/support/lists/">mailing lists page</a>.
          The mailing lists are "opt-in". Due to the prevalence of spam and
          security concerns, if you wish to subscribe to a mailing list them
          then only way that you can do it is to send a message from the address
          that you wish to subscribe.
        </p>
        <p>
          For security reasons, license codes are permanently bound to the email
          address used at the time of purchase. This is to reduce the likelihood
          of an outsider attempting to "hijack" your license by claiming to be
          you and then trying to change the address associated with the license.
          What this means is that when you enter you enter your license code in
          the software you must use the address exactly as it appears on the
          registration notice. It's very important to keep backups of this
          notice (both printed and electronic) so that if you ever lose access
          to your email address then you will still have a copy of your
          registration information.
        </p>
        <p>In summary:</p>
        <ul>
          <li>
            Your mailing list subscriptions are completely independent of your
            registration, and you can
            <a href="https://wincent.com/a/support/lists/"
              >change them at will</a
            >.
          </li>
          <li>
            Your license code and email address used at the time of purchase are
            an immutable pair, and so it's critical that you keep a back-up of
            your registration notice if you plan to stop using the email account
            you used at the time of purchase.
          </li>
          <li>
            If you want to notify me of a change of address, for my own records,
            you can use
            <a href="https://wincent.com/a/support/update-address/">this form</a
            >. I will keep this information on file, but I won't be able to
            issue you with a new license-code or change your mailing list
            subscriptions for you (you must do that yourself).
          </li>
        </ul>
        <h4>I want to report a problem with the website</h4>
        <p>
          If you're having trouble finding things on the website try looking on
          the <a href="https://wincent.com/a/site-map/">Site map</a> or
          performing a
          <a href="https://wincent.com/a/site-map/search/">Search.</a>
        </p>
        <p>
          If you think you've found a broken link, a mistake, or a technical
          problem on the website, please use the
          bug tracking database.
        </p>
        <p>
          For more information about the bug tracking database, see the notes on
          reporting bugs above.
        </p>
        <h4>General feedback and enquiries</h4>
        <p>
          If none of the alternatives listed above are suitable, you are welcome
          to use the
          <a href="https://wincent.com/a/contact/mail/">mail form</a> to send me
          an email. Due to the number of emails received, I may not be able to
          write personalized replies to each and every message, although I do
          try to do so. Rest assured that if your enquiry is one which asks a
          question or requires some other kind of response, you
          <em>will</em> receive a reply. Where appropriate I send semi-automated
          replies using the excellent
          <a href="http://www.abracode.com/MailTemplate/"
            >MailTemplate software</a
          >
          from <a href="http://abracode.com/">Abracode</a>. This allows me to
          send a standard, pre-written reply to frequently received enquiries.
        </p>
