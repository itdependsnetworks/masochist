          <h3>Product activation</h3>

          <p>
            All new products released <em>after</em> April 2005 use product
            activation; this includes
            <a href="../../products/synergy-advance/">Synergy Advance</a> and
            Hextrapolate.
            Products released <em>prior</em> to April 2005 &mdash;
            <a href="../../products/synergy-classic/">Synergy</a>,
            <a href="../../products/winswitch/">WinSwitch</a> and
            <a href="../../products/install/">Install</a> &mdash; do not feature
            product activation.
          </p>

          <h4>What is Product Activation?</h4>

          <p>
            Product activation takes place when you enter your license code in a
            Wincent product (but note that not all Wincent products use product
            activation yet). The program connects once (and once only) to the
            wincent.com webserver to validate the license and the product is
            then activated permanently. If you later install the product on
            another machine, then you will need to activate the product once
            more. Activation requires nothing more than a click of button and
            takes only a few seconds (<a
              href="sample-b.jpg"
              title="See sample screenshot in separate window"
              target="_blank"
              >see a sample screenshot from a development version of Synergy
              Advance</a
            >; the email address and license address have been blurred for the
            purposes of the screenshot only).
          </p>

          <h4>Why use Product Activation?</h4>

          <p>
            The Product Activation system is the result of nearly two years'
            engineering work with the goal of reducing the damage done by
            software piracy <em>without</em> inconveniencing honest users. Much
            thought has gone into implementing the system in a clean and elegant
            fashion; whereas in older products users entered their license codes
            and clicked an &quot;OK&quot; button, they now click a
            &quot;Verify&quot; button. The only difference is that their license
            is now verified with the wincent.com server and after few seconds or
            less the product is permanently activated. No personal information
            is transmitted to the Wincent server in the process so your privacy
            is protected (read the
            <a href="privacy/">Activation Privacy FAQ for more details</a>).
          </p>

          <p>
            In the past piracy has had a heavy impact on Wincent. One of the
            great things about the Internet is that it makes it very easy to
            distribute software to a large number of people. Unfortunately for
            software developers it's also very easy to distribute license codes
            and cracks. A single leak can quickly lead to thousands of pirated
            copies. Product Activation minimizes the damage caused by such
            leaks. If the same license code is used to activate a product
            hundreds or thousands of times, then it's clear that a leak has
            occurred and further activations can be stopped before the damage
            spreads too far.
          </p>

          <p>
            Product activation is employed by some of the largest software
            companies in the world such as Microsoft, Macromedia, Adobe, Network
            Associates and Symantec and the list is constantly growing. In the
            not-too-distant future product activation is likely to be included
            in the majority of commercial software. (You can read more general
            information about product activation and the companies that use it
            on
            <a href="http://www.bsa.org/usa/antipiracy/Product-Activation.cfm"
              >this page</a
            >
            at the
            <a href="http://bsa.org/">Business Software Alliance website</a>.)
          </p>

          <h4>How does it work?</h4>

          <p>
            When you install a product for the first time it generates a random
            number that is unique to the machine on which the software is being
            installed. The number is what is known as a one-way cryptographic
            hash; it's so called because the calculation only works in one
            direction: it is easy to generate the number based on the machine,
            but it is impossible to look at the number and know which machine it
            came from.
          </p>

          <p>
            A hash is also made from your license code. The one-way nature of
            the hash means that if somebody were to see it he or she would have
            no way of figuring out what your license code was; nevertheless, the
            hash is a unique number and no two licenses will produce the same
            hash. For more information about how these cryptographic hashes
            guarantee your privacy see the
            <a href="privacy/">Activation Privacy FAQ</a>.
          </p>

          <p>
            These two pieces -- let's call them the "machine hash" and the
            "license hash" -- are sent to the Wincent server at activation time.
            If the server sees many activation requests coming in with the same
            license hash but lots of different machine hashes, then it knows
            that the software is being activated on many different machines with
            the same license code. Note that because one-way hashes are used in
            the process noboby can personally identify machines or customers;
            the numbers are just random ID numbers that cannot be traced back to
            a particular individual or computer.
          </p>

          <p>
            The activation server permits a reasonable number of activations for
            any given license code. For example, you may buy a new machine and
            wish to install the software on the new machine in which case you'll
            perform the product activation on the new machine. (If you need to
            to perform additional activations above the limit then you can
            <a href="https://wincent.com/a/support/tickets/"
              >open a support ticket</a
            >.) Similarly, some may buy multi-user licenses and need to activate
            on a large number of machines; in this case also the activation
            server knows that it should allow a higher number of activations.
            See
            <a href="#limits">"How many activations may I perform?"</a> below
            for more information.
          </p>

          <p>
            When the server approves an activation it sends a reply to the
            connecting program containing a certificate of activation. The
            program stores the activation certificate along with the program
            preferences. From that point onwards the program will know that it's
            been activated and you won't have to perform the activation again
            (although if you ever format your hard disk, modify your machine
            sufficiently for it to appear like a "new machine" or otherwise
            delete the activation certificate you'll need to reactivate).
          </p>

          <h4>When does it happen?</h4>

          <p>
            Wincent products will never &quot;phone home&quot; without your
            permission. Product Activation occurs when you first enter your
            license code and you press the &quot;Verify&quot; button. The user
            interface for entering your license code makes it very clear what
            will happen, so there are no surprises (see these sample
            screenshots:
            <a
              href="sample-a.jpg"
              title="See sample screenshot in separate window"
              target="_blank"
              >Sample A</a
            >,
            <a
              href="sample-b.jpg"
              title="See sample screenshot in separate window"
              target="_blank"
              >Sample B</a
            >). If you cannot connect to the Internet to perform the activation,
            the software will remind you to do so the next time you run it.
          </p>

          <h4>What happens if the network is down when I try to activate?</h4>

          <p>
            The software will still run without activation so if you have a
            temporary network failure you can try again later at your
            convenience. The software will remind you to do so the next time you
            run it. Once the software has been activated it does not need to
            connect to the Internet again so there is no dependency on a network
            connection.
          </p>

          <p>
            The wincent.com activation server runs an industry-standard
            UNIX-like operating system (<a href="http://www.redhat.com/"
              >Red Hat Enterprise Linux</a
            >) and is hosted by
            <a
              href="http://service.bfast.com/bfast/click?bfmid=30735717&amp;siteid=41506187&amp;bfpage=hosting_headaches"
              >Rackspace</a
            >, regarded by many as the most reliable hosting provider in the
            world (for more on Rackspace,
            <a
              href="https://wincent.com/a/about/wincent/weblog/archives/2005/11/some_words_of_p.php"
              >see this article</a
            >). Their data centers have uninterruptable backup power generators
            and multiple, redundant, high-bandwidth connections to the Internet.
            Rackspace guarantees a "Zero-Downtime Network&trade;". In the
            unlikely event that you cannot connect to the activation servers
            during maintenance or due to a transitory network fault between your
            machine and the servers you will find that you can continue to use
            the software and perform the activation later.
          </p>

          <h4>What happens if Wincent stops trading?</h4>

          <p>
            So what happens if Wincent stops trading at some point in the future
            and the activation server goes off-line forever? How can people
            continue to enjoy the software they've licensed if they can't
            activate it?
          </p>

          <p>
            Wincent has no plans to discontinue trading but recognises that this
            is a frequently voiced concern about product activation and so makes
            the following undertaking, backed by Wincent's reputation as an
            online vendor <a href="https://wincent.com/a/about/">since 1997</a>:
          </p>

          <p>
            In the event that Wincent should stop trading it will do one of the
            following at its discretion:
          </p>

          <ol>
            <li>
              Make available for free download updated versions of all products
              with the product activation requirements removed. These versions
              would be freely redistributable.
            </li>
            <li>
              Make available special license codes which turn off the product
              activation requirement. These codes would be freely
              redistributable.
            </li>
            <li>
              Release the source code for the protected applications under an
              open source license such as the GPL or the BSD license.
            </li>
          </ol>

          <h4>Am I being accused me of being a thief?</h4>

          <p>
            Most definitely not. The vast majority of people who purchase
            licenses are honest users who would never think of sharing their
            license code in breach of their license agreement. Product
            activation is <em>not</em> about accusing honest users of being
            thiefs. Product activation <em>is</em> about minimizing the damage
            done by an extremely small minority of dishonest users. This
            minority makes everyone else worse off, because widespread piracy
            eats into sales which in turn means that software companies have
            fewer resources available for development; the consequence is then
            fewer products, and less updates. Product activation addresses the
            problem caused by this minority and benefits everyone else because
            profitable software companies have more resources to pour into
            improving their products and releasing them sooner.
          </p>

          <p>
            The Wincent product activation system has been developed according
            to some best practice guidelines (<a href="#practices">see here</a>)
            whose goal is to ensure that honest users are <em>not</em> penalized
            or inconvenienced. The idea is to make life difficult for those who
            would break the rules, and make life easy for the good guys.
          </p>

          <h4>
            <a id="limits" name="limits"></a>How many activations may I perform?
          </h4>

          <p>
            At the time of writing (September 2006) the number of activations
            that may be performed for a single-user license is five. A
            single-user license permits you to install and run the software on a
            single machine, but the activation limit is set to five activations,
            not one, to allow for scenarios such as switching to a new machine,
            replacing your hard drive, or performing a full system re-install.
            This is because the spirit of the activation system is to target
            mass piracy, not penalize honest, paying customers.
          </p>

          <p>
            The number of activations permitted may be adjusted in the future
            based on experience and feedback from customers. Since the release
            of
            <a href="https://wincent.com/a/products/synergy-advance/"
              >Synergy Advance</a
            >
            in May 2005, a five-activation limit has proved to be appropriate in
            the vast majority of cases. An automatic system is in place that
            performs a yearly revision of each license and that grants users an
            additional activation per license, per user, per year; this should
            fit in nicely with the typical pattern in which users upgrade to new
            machines once every year or two. The additional activation(s) are
            applied to any license that is in good standing (activation limit
            not exceeded) and which has been activated at some point in the
            previous 12 months.
          </p>

          <p>
            If you exceed the activation limit, you may
            <a href="https://wincent.com/a/support/tickets/"
              >open a support ticket</a
            >
            and request that your limit be reset.
          </p>

          <h4>
            <a id="practices" name="practices"></a>Product activation best
            practices
          </h4>

          <p>
            The Business Software Alliance has published a set of best practice
            guidelines for Product Activation. The Wincent Product Activation
            fulfills or exceeds each and every one of the guidelines:
          </p>

          <h5>Respect for Privacy</h5>

          <p>
            Activation should not collect or transmit personally identifiable
            information without the users' explicit knowledge and should comply
            with applicable data protection laws.
          </p>

          <h5>Ease of Use</h5>

          <p>
            Activation methods should not be burdensome to the user. Ideally,
            they should be limited to a one-time step or process per machine.
          </p>

          <h5>Full Disclosure</h5>

          <p>
            Publishers should clearly explain the activation process, including
            the activation data and how it is transmitted, so that users
            understand how activation works and are assured that their privacy
            will be respected.
          </p>

          <h5>Support</h5>

          <p>
            Documentation and access to high quality support should be made
            available to assist users with activating their product and to
            answer their questions.
          </p>

          <h5>Security</h5>

          <p>
            The activation solution should use appropriate security to protect
            stored and transmitted activation data.
          </p>

          <h5>Speed</h5>

          <p>
            The activation process should be completed as quickly as possible.
          </p>

          <h5>User Control</h5>

          <p>
            Activation should empower users to easily move their software
            program to a new or upgraded computer in a manner authorized by the
            license agreement.
          </p>
