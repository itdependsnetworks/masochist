          <h4>
            <a id="licenseCode" name="licenseCode"></a>I didn't receive my
            license code; where is it?
          </h4>

          <p>
            Once you have paid you will receive your license information via
            email. Messages are sent by a fully-automated system so you should
            receive the message within seconds or minutes of completing your
            payment depending on network conditions. If after waiting several
            minutes you still have not received your license notice check the
            following:
          </p>

          <ul>
            <li>
              <em>Have you double-checked your registration email address?</em>:
              The most common reason for not receiving a license notice is
              specifying an incorrect or out-of-date email address. Please log
              in to your <a href="https://www.paypal.com/">PayPal</a> account
              (if you paid via PayPal) or double-check with
              <a href="http://info.kagi.com/">Kagi's records</a> (if you paid
              via Kagi) and confirm the email address you have specified in your
              settings. You will need to check that account in order to receive
              your license notice. If the email address is out-of-date and you
              no longer have access to it you can
              <a href="https://wincent.com/a/support/tickets/"
                >open a support ticket</a
              >
              and request that your license code be sent to a newer address
              (please supply your full name, old email address, new email
              address, product purchased, purchase date, and receipt or
              transaction number).
            </li>
            <li>
              <em>Have you checked your junk mail filters?</em>: Check that your
              license message did not get falsely identified as spam and
              diverted to your junk mail folder.
            </li>
            <li>
              <em>Was the message blocked by a challenge-response system?</em>:
              If you have a challenge-response system in place it may have
              bounced your license notice and prevented it from being delivered.
              Given that license notices are sent by an automated system there
              is no human that could respond to such a challenge; you'll
              therefore need to check your quarantined messages or add the
              appropriate wincent.com address to your "whitelist". Please see
              the
              <a href="https://wincent.com/a/about/spam/"
                >Wincent Anti-spam Policy page</a
              >
              for a list of outgoing wincent.com addresses that you may wish to
              add to your whitelists.
            </li>
            <li>
              <em>Did you pay via eCheck?</em>: If you paid using a PayPal
              "eCheck" then the transaction will not be completed until the
              payment has cleared. Just as it would for a real-world check,
              PayPal imposes a clearance delay of several days (typically about
              four business days) before allowing the funds to clear and the
              transaction to be completed. Immediately upon clearance PayPal's
              automated systems will cause your license notice to be sent to you
              via email. There is no human intervention required so there should
              be no additional delays.
            </li>
          </ul>

          <p>
            You can have your license code resent to you at any time by using
            the
            <a href="https://wincent.com/a/support/registration/form.php"
              >lost license code form</a
            >
            (this is an automated system).
          </p>

          <p>
            Note that the automated systems apply to all Wincent products with
            the exception of Install;
            Install purchases are processed manually because they require
            special access to the Wincent server and must be set up manually.
            The expected processing times appear on the
            <a href="https://wincent.com/a/products/install/purchase/"
              >Install purchase page</a
            >.
          </p>

