          <h4>
            <a id="which" name="which"></a>Which payments service should I use?
            PayPal or Kagi?
          </h4>

          <p>
            Wincent recommends <a href="http://www.paypal.com/">PayPal</a>, a
            very popular payments service with over 100 million users (as of
            October 2006) and available in over 103 countries including the
            United States. PayPal will automatically handle any currency
            conversions for you and they do not charge VAT on purchases made
            from within the European Union. Using PayPal you will be able to pay
            using a variety of means including credit card, "eCheck" and Bank
            Transfer (please note that in the case of "eCheck" and "Bank
            Transfer" payments your transaction will only be classified as
            "Completed" once your check or transfer has cleared. PayPal advises
            that it takes approximately four business days for checks to be
            marked as cleared).
          </p>

          <p>
            <a href="http://www.kagi.com/">Kagi</a> is provided as an
            alternative payment processor. Kagi is less popular but it does have
            a long history and an excellent reputation as a payments service for
            Macintosh software. Please note that Kagi collects VAT on all
            purchases made from within the European Union which makes PayPal a
            better choice for people living there (see the
            <a href="#vat">following FAQ</a> for more information about VAT). In
            addition, Kagi's higher merchant processing fees drive up the
            license prices; for most products Wincent absorbs these processing
            costs but in the case of products with extremely low prices such as
            <a href="https://wincent.com/a/products/synergy-classic/"
              >Synergy</a
            >
            there is a price differential (5 Euros for licenses purchased
            through PayPal but 10 Euros for those purchased via Kagi). Kagi
            provides many payment alternatives such as money order and cash
            which makes it an excellent choice for people who do not have an
            appropriate credit card or bank account to use with PayPal.
          </p>

