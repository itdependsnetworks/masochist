        <h3>Registering Wincent software</h3>

        <p>
          Once you have paid you will receive your license information via
          email. Messages are sent by a fully-automated system so you should
          receive the message within seconds or minutes of completing your
          payment depending on network conditions. If after waiting several
          minutes you still have not received your license notice please see
          <a href="https://wincent.com/a/support/faq/#licenseCode"
            >this question</a
          >
          in the
          <a href="https://wincent.com/a/support/faq/"
            >Frequently Asked Questions</a
          >.
        </p>

        <h4>Copying license information from your email</h4>

        <p>
          Your license notice indicates your registered email address (next to
          the label, <tt>EMAIL ADDRESS:</tt>). To prevent other people from
          using your license, license codes are cryptographically tied to the
          email address specified at the time of purchase. This means that when
          registering you must enter your email address exactly as it appears on
          the license notice. If you enter a different email address it will not
          work.
          <strong
            >Given the need for accuracy it is recommended that you use copy and
            paste to enter your email address in the purchased
            application.</strong
          >
        </p>

        <p>
          It's very important to keep backups of your license notice (both
          printed to paper and electronic, preferably burnt to a CD or DVD) so
          that if you ever lose access to your email address then you will still
          have a copy of your registration information. For security reasons,
          license codes are permanently bound to the email address used at the
          time of purchase. This is to reduce the likelihood of an outsider
          attempting to "hijack" your license by claiming to be you and then
          trying to change the address associated with the license. If you want
          to notify me of a change of address, for my own records, you can use
          <a href="https://wincent.com/a/support/update-address/">this form</a>.
          I will keep this information on file, but I won't be able to issue you
          with a new license-code for the reasons already stated.
        </p>

        <p>
          Your license notice also indicates your unique license code (next to
          the label, <tt>LICENSE CODE:</tt>). The code is quite lengthy because
          it is generated using strong cryptography.
          <strong
            >Given the length of the code it is recommended that you use copy
            and paste to enter it in the purchased application.</strong
          >
          The following screen snippet shows a sample license code already
          highlighted in the mail reader, ready to be copied and pasted into the
          target application.
        </p>

        <div class="screenshots">
          <img
            src="license-email.png"
            alt="Emailed license information"
