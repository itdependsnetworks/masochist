            height="160"
          />
        </div>

        <h4>Entering your email address</h4>

        <p>
          The following screen snippet shows what the registration panel looks
          like after pasting the sample registered email address into the first
          text field. Note that the cursor has been moved down into the first
          text field of the license code area, ready for pasting.
        </p>

        <div class="screenshots">
          <img
            src="entering-license-1.png"
            alt="Preparing to enter license code"
            width="531"
            height="429"
          />
        </div>

        <p>
          You don't have to worry about any additional leading or trailing
          whitespace when performing the copy-and-paste as this will
          automatically be stripped for you. Email addresses are not
          case-sensitive, so you don't have to worry about that either.
        </p>

        <h4>Entering your license code</h4>

        <p>
          The screen snippet below shows what the registration panel looks like
          after pasting the sample license code. Note that it is not necessary
          (or desirable) to type out the entire code manually, nor should you
          paste the code in separate chunks. You can paste the entire code in
          one go and the software will automatically distribute it correctly
          across all of the text fields in the matrix. The text fields are for
          visual separation only; in reality they behave together like one big
          text field.
        </p>

        <div class="screenshots">
          <img
            src="entering-license-2.png"
            alt="Preparing to enter license code"
            width="530"
            height="428"
          />
        </div>

        <p>
          You don't have to worry about any additional leading or trailing
          whitespace when performing the copy-and-paste as this will
          automatically be stripped for you. License codes are not
          case-sensitive, so you don't have to worry about that either.
        </p>

        <h4>Verifying your license</h4>

        <p>
          Once you have copied and pasted both the registered email address and
          license code you can click the "Verify" button to complete the
          registration process. This is a two step process:
        </p>
        <ol>
          <li>
            <em>The application checks that your license code is valid:</em> if
            the application reports that your license code is invalid, please
            double-check that you have correctly pasted both the registration
            email address and the license code from your license notice. If
            after double-checking you are still having trouble try asking a
            friend to help; often a second pair of eyes can spot a mistake that
            may not be obvious to you. If you're unable to enter your license
            code even after asking a friend you can
            <a href="https://wincent.com/a/support/tickets/"
              >open a support ticket</a
            >
            (although please be aware that the advice you receive is unlikely to
            go very far beyond the information already on this page!).
          </li>
          <li>
            <em>The application performs product activation</em>: this is a
            once-only process that is fully described on the
            <a href="https://wincent.com/a/support/activation/"
              >product activation page</a
            >
            and the
            <a href="https://wincent.com/a/support/activation/privacy/"
              >product activation privacy FAQ</a
            >. If you are having troubles with product activation after
            consulting those pages please feel free to
            <a href="https://wincent.com/a/support/tickets/"
              >open a support ticket</a
            >.
          </li>
        </ol>
