            <p>
              Other, less expensive software that I've purchased for work
              includes <a href="http://ecto.kung-foo.tv/">ecto</a> (in fact, I'm
              typing in it right now),
              <a href="http://www.interarchy.com/main/">Interarchy</a>,
              <a href="http://babelcompany.com/impression/index.html"
