            <h4>Domain names</h4>

            <p>
              At the beginning you may not even require a domain name. You might
              be happy with "homepage.mac.com/johnsmith/" as your website
              address. In order to market your software, you will eventually
              want your own domain name. There's really no limit to how far you
              can go with this. You may choose to buy a different domain for
              each product, and you can also opt to get not only the dot-com
              name, but the dot-net and dot-org as well.
            </p>

            <p>
              In my own case, I own quite a few domain names, but I direct them
              all to this one site, typechecked.net, because I want to build the
              Wincent brand.
            </p>

            <h4>SSL certificates</h4>

            <p>
              If you want to support SSL connections over HTTPS, then you'll
              need an SSL certificate as well.
            </p>

            <h4>Post office box, phone, premises</h4>

            <p>
              At some point you'll decide that you need an official physical
              presence in the world; not just a "cyber" presence. You can only
              use you're mother's mailbox for so long. Eventually you'll want
              your own post office box, phone line, and perhaps one day even
              leased office space. One of the beauties of Internet-based
              software business is that you <em>can</em> start off in your
              basement or bedroom if you need to, and the nature of the business
              is flexible and you can adapt it over time depending on how
              quickly (and even if) you grow.
            </p>

            <h4>Payment processing fees</h4>

            <p>
              <a
                href="http://www.macdevcenter.com/pub/a/mac/2002/10/25/dev_osx.html"
                >This article</a
              >
              over at
              <a href="http://www.macdevcenter.com/">macdevcenter.com</a>
              reviews some of the payment processing services available to you.
              Personally I only have experience with
              <a href="http://www.kagi.com/">Kagi</a> and
              <a href="https://www.paypal.com/">PayPal</a>.
            </p>

            <p>
              PayPal's fees are lower, and so they enable you to set your price
              point lower, which in turn makes PayPal the most popular option
              with customers (even though many people have misgivings about
              PayPal). If someone donates you a dollar, you'll lose about half
              of it in fees, but if they pay you for a $5 product then you'll
              only lose 50 cents. You can withdraw the money into your bank
              account whenever you want for a $1 fee.
            </p>

            <p>
              You'll still want an alternative processing service, for those who
              don't like PayPal or can't use it, but the costs will be higher
              for both you and the customer. To use Kagi as an example; consider
              the product that sells for $5 via PayPal; if you were to sell it
              for $5 via Kagi you'd have only $2.50 after fees. Furthermore
              you'd have to wait a long time before you got access to the funds
              (for example, a payment made on 1 December wouldn't be mailed to
              you until 20 January, and if the cheque is being sent overseas
              then you will lose $10 for banking a foreign currency cheque, five
              to seven days for transit time, and 28 days for the funds to
              clear. What this means is that for the customer who paid you for
              the license on 1 December, you only actually get your hands on the
              funds three months later. This is effectively a cost to your
              business because not only do you miss out on earning three months'
              interest on that money, but you could well end up paying interest
              in the meantime because you are more likely to have to pay your
              bills using credit).
            </p>

            <p>
              Another additional cost with Kagi is that they charge VAT (Value
              Added Tax) to all customers living in the European Union, even if
              the company selling the software is located outside the EU and has
              a turnover that is below the threshold for VAT. I don't have the
              figures on me right now, but you can turn over something like
              $100,000 per year before you're officially required to collect
              VAT. So this means that a lot of customers are needlessly paying
              as much as 25% above the price that they should be paying when
              they use Kagi.
            </p>

            <p>
              The difference between Kagi and PayPal is most noticeable to the
              customer when the product is a low-priced one. For example,
              <a href="https://typechecked.net/a/products/synergy/">Synergy</a>
              costs only 5&euro; when you purchase through PayPal, but $10 when
              you purchase through Kagi (plus VAT if you are unfortunate enough
              to be penalized for living in the EU). On a higher priced product,
              one that cost $25 or more, you could afford to equalize the prices
              across the board and absorb any differences so that the customer
              isn't affected.
            </p>

            <p>
              Another alternative is to get your own merchant bank account and
              hook yourself up with a payment gateway (for example, see
              <a href="http://authorize.net/">authorize.net</a> for a list of
              <a href="http://authorize.net/reseller/directory.php">resellers</a
              >).
              <a href="http://www.usms.com/">Some of those resellers</a> will
              open accounts for you even if you aren't in the USA.
            </p>

            <p>
              Having your own payment gateway means that you can bypass PayPal
              and Kagi with all of their fees and technical problems, but as
              always, there is a cost. There's no protection from chargebacks
              and you have to develop the server-side software (or purchase it)
              to talk to the gateway. This can be a costly exercise, but as you
              move along the scale from hobby to serious, it eventually becomes
              unavoidable. Would a company like Adobe or Microsoft use PayPal or
              Kagi to collect payments for their software? There's evidently a
              threshold somewhere, and one day you'll have to cross it.
            </p>

            <h4>Wages</h4>

            <p>
              Generally when you start of there'll be only one employee: you.
              But even when you are self-employed you still have to worry about
              wages. Of course, you'll have to pay bandwidth bills and the other
              costs that we've already discussed, but at the end of the day you
              are not in business if the activity you're engaging in doesn't end
              up putting money into your pocket. That money which, figuratively
              speaking, "goes into your pocket" is your wage. It goes without
              saying that unless you have a wage you won't be able to pay the
              rent or buy food to eat!
            </p>

            <p>
              You need to look at your time in terms of its dollar value. For
              example, in Australia, I can earn A$10 (US$7.50) per hour if I
              work "cash in hand" at a video store. If I work in a bookstore, I
              can earn almost A$17 an hour (before tax). These are both jobs
              that I've worked at in the past.
            </p>

            <p>
              When it comes to running your software business, you have to think
              in terms of the value of your time. So, I'll take that base figure
              of A$10 per hour. For every hour that I devote to software when I
              could have been working in the video store or the book store, I
              will have to find A$10 from somewhere to help me pay the rent, to
              recover the money that I didn't earn. In other words, if you
              expect to continue in business, you have to make sure that each
              hour you spend working on your business earns you $10. This is
              actually a pretty modest figure, compared to what a consultant, a
              doctor, a lawyer, or other professional would charge per hour. In
              fact, it's much less than a plumber or skilled tradesperson
              charges. So $10 is a realistic goal, but you are going to need
              every penny of it in order to meet all those costs.
            </p>

            <p>
              Of course, when you work on your own, you don't actually "pay
              yourself a wage" as such. There's no pay packet at the end of the
              week, the fortnight or the month. You take your income, pay your
              bills, and hope that there's some left over at the end. And along
              the way you use credit where necessary to ride you through the
              rough patches, and you try to accumulate a safety net when things
              are going well.
            </p>

            <h4>Tax</h4>

            <p>
              If you ever do get to the point where there is some money left
              over at the end of the month, you've officially got an income in
              the eyes of the taxation office. When you start off you might
              spend $5,000 in a year and only sell $1,000 of software. There's
              no tax to pay. Further along you might spend $5,000 but sell
              $10,000 in the year, and then you will indeed have to pay tax on
              the $5,000 that was left over, just as you would have to pay tax
              if you earned that money working as a sales assistant or a
              teacher.
            </p>

            <h4>Developer Program Membership</h4>

            <p>
              You'll want to join the
              <a href="https://connect.apple.com/">Apple Developer Connection</a
              >, or ADC. You can get a free online membership, but it really
              doesn't offer anything of much value. To get access to hardware
              discounts, software seeds, and regular mailings, you'll need to
              pay at least US$500 (annually) to get Select membership.
            </p>

            <p>
              It <em>is</em> a lot of money, but I do think it's worth it,
              especially if you can time it so that it coincides with a hardware
              purchase (the hardware discount could even be enough that the ADC
              membership ends up "paying for itself", relatively speaking).
            </p>

            <p>
              I started off as a free "Online" member and bought my first Select
              membership in November last year. Since then I've really
              appreciated the mailings, more than anything. It's also nice to
              receive the occasional copy of the operating system on official
              media. At the moment I only have one machine, so I am not about to
              go installing Tiger on it (I need my production machine to be
              totally stable); but if I did have another machine I think the
              seeds would be a valuable resource too.
            </p>

            <p>
              The next step up from Select membership is Premier membership,
              which includes even more benefits, but with a whopping price tag.
              Definitely not for the small shareware operator. An intermediate
              step would be to go to WWDC (the World Wide Developers
              Conference), but that too had a hefty price tag ($1,500) last time
              I checked, especially when you add in airfares and accommodation.
            </p>

            <h4>Office supplies</h4>

            <p>
              A small cost compared to the others, there's still no avoiding the
              need for blank media, paper, envelopes, stamps, and the other
              material you'll need in the day-to-day running of the business.
            </p>

            <h4>Advertising</h4>

            <p>
              Luckily, you can probably do without paid advertising for some
              time because the Internet is based on communities. There are lots
              of opportunities for free promotion and for "word of mouth"
              advertising. Generally, the more popular your software becomes,
              the more it will start to promote itself. Just make sure you've
              got your website in order when the visitors come!
            </p>

            <p>
              Of course, with increased attention comes increased competition.
              If you have a good idea expect it to be copied time and time again
              by people who can't come up with creative ideas of your own. The
              only remedy for this is to keep one step ahead as far as good
              ideas are concerned and hope that you have the time to implement
              them all! Legal warfare is probably not an option for the small
              operator!
            </p>

            <h4>Piracy</h4>

            <p>
              When I worked in the bookstore, shoplifting was a real problem,
              but I learnt a very valuable lesson from the owner of that store.
              Every day he knew that dozens of books, worth hundreds of dollars,
              were being ferreted illegally out of his store. He was an
              owner/operator, so it really was money from <em>his</em> pocket
              that was walking out the door.
            </p>

            <p>
              But what really impressed me was that he always kept his cool. Of
              course, if someone was detected shoplifting he apprehend them and
              called the police, if he could. But he was always cheery and he
              never became obsessed with the theme that every hour at least a
              few of his books were being stolen from the shop. He had every
              reason in the world to have high blood pressure, yet he didn't
              have it. And this was because he viewed theft as one of the costs
              of the business, just like electricity or rent. You wouldn't lose
              sleep because you have to pay for electricity, would you? I think
              that we can productively compare shoplifting with software piracy.
            </p>

            <p>
              There are lots of ways to look at piracy, depending on which side
              of the fence you're standing on: as a crime, as a fact of life, as
              a harmless pastime, as a game, and so forth. But as a business
              person, the only sensible way to look at piracy is
              <em>as a running cost</em>.
            </p>

            <p>
              Piracy is basically unavoidable, like paying for electricity. If
              you suffer a demonstrable 5 or 10% loss in sales because of
              piracy, then you're probably just like every other software
              company in the world. You can think about ways to minimize that
              loss (just like you can try to minimize your electricity bill),
              but you'll never escape it entirely.
            </p>

            <p>
              The problem is when that percentage gets out of control, as it has
              of late with my software. Over the last few months I've seen sharp
              drops in sales which correspond with periods when license codes
              are mass-distributed, or cracks start to circulate. You can match
              up the days on which leaks occurred with drop-offs in the sales;
              and you can watch the sales recover bit by bit when you blacklist
              license codes or block cracks. But once you've started to lose the
              war, the game is pretty much over. You'll have to resign yourself
              to about 30% loss in sales until you can completely re-engineer
              your protection.
            </p>

            <p>
              So the goal should be to have adequate piracy protection that
              keeps the damage done to a manageable level (like 5 or 10%) and
              not spiraling out of control (30% or more).
            </p>

            <h4>General costs</h4>

            <p>
              If you work on your own you won't have to pay other people's
              wages, but you will have to do all the work yourself! Imagine all
              that money you'll save when you don't have to employ a UNIX
              systems administrator to set-up and manage your web, mail and
              database servers; when you don't have to pay a web designer or
              graphic artist; when you don't pay an accountant; when you don't
              have to pay for customer support staff, and all the rest.
            </p>

            <p>
              Of course, the other side of that equation is that all of those
              jobs still have to be done (and more). And you are the person who
              will be doing them! And if you remember the point made above about
              the equivalency of time and money (your time being valued at $10
              an hour), you've got to remember that every hour you spend doing
              UNIX administration, every hour you spend doing accounting, and so
              forth, effectively costs you $10.
            </p>

            <p>
              You have to carefully evaluate where you spend your time, in terms
              of whether or not it will bring you the long term pay off. Ask
              yourself, if I spend an hour doing my tax return, will that time
              be well-spent, financially? (Of course, the answer is yes; if you
              don't do your tax return you might wind up getting fined or thrown
              in jail!). Ask yourself, if I spend an hour upgrading the mail
              server software, will that time be well-spent? And if I design new
              graphics for the web site? And if I spend a day learning how to
              set up a master and slave database replication setup?
            </p>

            <p>
              All of these questions have to be weighed up. It's one of the
              great things about the job: the variety; but you have to remember
              that ultimately every hour you spend has to be justified in the
              long term, and it has to have be financially viable, in the "big
              picture" sense, otherwise you won't be able to keep doing what you
              love to do (developing software). This very article is a good
              example of just one of those evaluations. Why have I spent a
              couple of hours ($20) writing an article for which no one is going
              to pay me? The answer is that I do it because I think it adds
              value to the site, helps to build a community here, and I think
              that in the long-run that having a community following at
              typechecked.net is in my best interests. And along the way I know that
              it's a good thing, because it might help other people, and helping
              other people is never bad.
            </p>

            <p>
              It can be a stressful thing at times, knowing that what you're
              doing has no immediate or tangible "pay off", and knowing that the
              rent will be due the next month and that above all else what the
              customers want is for you to work on <em>the software</em>. But
              it's a balancing act, because without all of the surrounding
              support work, the software wouldn't exist.
            </p>
