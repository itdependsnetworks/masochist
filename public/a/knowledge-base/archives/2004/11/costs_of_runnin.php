        <h2>November 11, 2004</h2>

        <h3>Costs of running a software small business</h3>

        <p>
          I'm writing this article for people who are thinking about starting up
          a business selling software on the Internet. These things often start
          out as hobbies, but as they become more serious, the costs (and
          potential benefits) start to rise as well. You may not be aware of
          some of these costs until you first stumble across them. So I'll list
          some of these costs as well as some hints on how to minimize them, and
          hopefully stay in business!
        </p>

        <div id="a000019more">
          <div id="more">
            <h4>Net access</h4>

            <p>
              Your customers will expect prompt replies from you, so that means
              having always-on net access from your place of business. It's also
              helpful from a development perspective to have access to online
              documentation when you need it. Broadband is strongly recommended,
              and is more or less essential if you hope to do large downloads
              (like Mac OS X software seeds or updates to the Xcode Tools).
            </p>

            <h4>Hosting</h4>

            <p>
              To distribute your software and provide information to potential
              users you'll need proper hosting for your website. It's important
              to go with a reputable ISP who can provide reliable connectivity.
              At the hobby end of the spectrum, you can use the web space that
              is most likely bundled with your Internet access. As your software
              becomes more popular you will probably overshoot the bandwidth
              allowance of your provider and have to move to something more
              substantial.
            </p>

            <p>
              You can pay for standard web hosting packages, virtual server
              packages, dedicated hosting (an entire machine all to yourself),
              and server colocation (where you provide the server and your ISP
              gives you space for it in its data center). As you move up the
              scale you pay more, but you get more control over the hosting. By
              the time you get into server colocation, you've got complete
              control over everything: you choose the OS and install it yourself
              and you configure and tailor every aspect of the server. This
              control comes at a price; you'll have to invest more time in
              setting things up, and the colocation fees will be high too.
            </p>

            <p>
              I think that I definitely broke with the normal pattern for
              shareware authors when I started out, because I went down the
              server colocation path back in 2002, before I even released my
              first product. At that point I had already been running the
              website for several
              years, out of my own pocket. I had had a long run of troubles with
              unreliable hosts: sharing the machine with thousands of other
              sites, unreliable or downright broken email, down time, lack of
              control, poor security. I was absolutely sick of it. For me it was
              worth it to buy a server out of my own funds and arrange
              colocation for me to be rid of all those problems. And I am very
              glad that I did it, as I never have any problems with the server.
              And if I do have a problem, it's always my own fault, and I can
              always fix it (rather than wait for someone else to fix it).
            </p>

            <p>
              No matter which hosting option you choose, you'll want to minimize
              your bandwidth costs. One of the best ways to do this is to
              recruit mirrors. In my own case this has been particularly
              critical because my server is in Australia, where bandwidth is
              still charged at the insane rate of 10 cents per megabyte. I also
              pay for hosting with
              <a href="http://www.pair.com/">pair.com</a> over in the US where
              bandwidth is cheaper. That server hosts many of
              the images for this website as well as downloadable files. By
              spending a little extra money in the US I can hopefully reduce my
              Australian bandwidth bill. Pair is not the cheapest, but they have
              a good reputation and that's important to me after my prior
              negative experiences. And I only get them to host vanilla files
              served straight from Apache (no MySQL or PHP).
            </p>

            <h4>Server hardware</h4>

            <p>
              If you go down the path of server colocation, as I did, you'll
              need to buy a server. It's possible to cut corners and save money
              here, but I don't think it's worth the peace of mind. I recommend
              buying a complete package from an established vendor, like
              <a href="http://www.ibm.com/">IBM</a>. Buy a 1RU (one rack unit)
              server, because ISPs often charge you more when your equipment
              takes up more slots in the rack.
            </p>

            <p>
              The IBM
              <a href="http://www.ibm.com/pc/us/eserver/xseries/">xSeries</a>
              presents remarkably good value for money. For example, at the time
              of writing you can pick up their xSeries 306 (equipped with a
              2.8GHz Pentium 4, 1MB cache, 512MB RAM, 36GB 10,000RPM SCSI disk
              drive, and CD-ROM) for $1,309, or less than half the cost of the
              cheapest <a href="http://www.apple.com/xserve/">Xserve</a> sold by
              Apple ($2,999). A machine like this is more than enough to serve
              the average website.
            </p>

            <p>
              Like most things in this business, as you get more serious you can
              (and probably should) spend more money. You may buy a single
              server the first time around, but as your business grows you may
              decide to purchase two servers the next time, and set them up in a
              redundant failover configuration, so that if one of them goes
              down, the other will automatically step in to ensure service to
              users isn't interrupted. You'll pay twice as much for colocation
              (because you'll use twice as much rack space), but the additional
              peace of mind is probably worth it.
            </p>

            <p>
              I haven't gone that far yet, but it is something I would
              definitely like to do. I <em>do</em> make off-site backups, but
              when my server is in Australia and I live in Spain, I would feel a
              whole lot more secure if I knew that I had a failover arrangement
              in place. I've done a bit of research on that topic, and will add
              another article to the
              <a href="https://typechecked.net/a/knowledge-base/">Knowledge Base</a>
              on that subject in the future.
            </p>

            <h4>Other hardware</h4>

            <p>
              When you start off as a hobbyist you presumably already have a
              computer. As you get bigger, you'll need to buy more hardware. For
              example, if you develop for more than one target operating system
              (for example, Jaguar, Panther and Tiger), you'll need additional
              partitions, drives or machines to do the testing. If you have a
              large customer base, you'll need a laptop so that you can stay in
              touch with your customers even when you're on the road.
            </p>

            <p>
              Even if you have only one machine, you'll want to make sure it's a
              good one. When you use your machine to make a living, its
              performance starts to matter. You may never play a game of Doom 3
              in your life, but if you find yourself waiting for your machine to
              catch up with your typing and mouse clicks, then you probably need
              a better machine. If it can't even keep up with your typing then
              just imagine how slow it will be at compiling and doing other
              things. Just ask me how I feel about working on a 1GHz PowerBook
              12" G4... By no means the slowest machine out there, but if I had
              a nickel for every time I have to wait for it to catch up with me
              in a day...
            </p>

            <p>
              And then of course there is the need to preserve data integrity.
              If you lose your family holiday snaps due to a hard drive failure
              then it's very sad; but if you lose all of your business data
              (your records, your source code) it could spell the end of your
              little enterprise. Unless you're prepared to see that data go
              bye-bye, then you'd be wise to invest in a UPS to protect against
              power failures and surges, some kind of RAID set-up (even if it is
              only twin mirrored drives using Panther's in-built software RAID),
              and a good backup mechanism (whether it be to external drives,
              burning to optical media, or tape).
            </p>

            <h4>Software</h4>

            <p>
              Luckily, the core development tools (Xcode Tools) are free, but
              there are a lot of non-free tools out there which will come in
              handy when developing your software, maintaining your website, and
              administering the business.
            </p>

            <p>
              Examples of commercial software that I've licensed for this
              website include
              <a href="http://www.movabletype.org/">Movable Type</a> (for
              various sections of this website, including the one you're reading
              now), and <a href="http://ubbcentral.com/">UBB.threads</a> (for
              the forums). Each of
              those will set you back a couple hundred dollars.
            </p>

            <p>
              Fortunately, some things are free, like the
              <a href="http://www.bugzilla.org/">Bugzilla</a>
              bug tracking and feature request database. The operating system on the server itself is free too (<a
                href="http://www.freebsd.org/"
                >FreeBSD</a
              >). Of course, if you want these great free packages to stay
              around then you really need to support them, for example by
              <a
                href="http://www.freebsd.org/doc/en_US.ISO8859-1/books/handbook/mirrors.html"
                >buying a CD or DVD set</a
              >.
            </p>

            <p>
              Other tools which you may end up finding indispensable, all of
              them with hefty price tags include
              <a href="http://www.barebones.com/products/bbedit/index.shtml"
                >BBEdit</a
              >,
              <a href="http://www.adobe.com/products/photoshop/main.html"
