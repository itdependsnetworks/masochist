          <h2>June 2, 2006</h2>

          <h3 id="a000346">WordPress security tip</h3>

          <p>
            Add the following to the <tt>.htaccess</tt> file at the top level of
            your <a href="http://wordpress.org/">WordPress</a> install:
          </p>

          <pre>
&lt;FilesMatch ^wp-config.php$&gt;
deny from all
&lt;/FilesMatch&gt;</pre
          >

          <p>
            This will make it harder for your database username and password to
            fall into the wrong hands in the event of a server problem. Better
            still would be to move the username and password outside of the
            document root entirely and <tt>include</tt> or <tt>require</tt> it
            but that would require you to turn off
            <a href="http://www.php.net/features.safe-mode">PHP Safe Mode</a>
            and/or the <tt>open_basedir</tt> restriction, which I don't
            recommend with a product like WordPress which is both popular and
            has a less-than-perfect security record.
