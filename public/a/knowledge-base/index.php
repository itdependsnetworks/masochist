          <h3 id="a000337">SSH tunneling and secure IMAP, POP3 and SMTP</h3>

          <p>
            During the time in which I was totally without Internet access last
            week I used iStumbler to see if there were any open, public wireless
            networks around which I could use to get on the Net.
          </p>

          <p>
            Turns out that there was one which was occasionally available, but
            with a very weak signal and I could only ever get a pitiful few
            bytes per second over it.
          </p>

          <p>
            When I first found this network I foolishly decided to check my
            email despite the fact that it uses inherently insecure protocols
            which transmit usernames and passwords in plaintext. I realized that
            the person running this open network could easily snoop them, so I
            decided to log into my server securely via SSH, immediately change
            the email passwords that had been transmitted as plaintext, and not
            try checking my email again until I had worked out a way to secure
            the protocols.
