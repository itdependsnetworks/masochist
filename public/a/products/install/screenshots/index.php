        <h3>Install screenshots</h3>
        <p>
          The Install user interface is carefully designed to provide users of
          all levels with a comfortable, informative and intuitive installation
          experience.
        </p>

        <p>
          As the screenshots below show, the elegant Aqua interface closely
          follows Apple's Human Interface Guidelines and builds on years of
          Macintosh tradition to deliver an uncluttered and functional UI which
          inspires confidence.
        </p>

        <p>
          Install provides a basic interface suitable for beginning users, but
          for those who like "power user" features it also provides several
          "expert" options which lie only a mouse-click away in the File menu,
          such as a file list window, a detailed log window, and the ability to
          reveal the files to be installed in the Finder.
        </p>
      </div>

      <div class="screenshots">
        <img
          src="https://wincent.com/a/products/install/screenshots/readMe.jpg"
          alt='"Read Me" step'
          width="641"
          height="448"
        />
        <p>
          A typical "ReadMe" Installer phase. The textual content of the window
          is easily defined (or localized) by creating a basic RTF file. The
          appearance of the text is easily controlled, including aspects such as
          whether or not to display scrollbars or a white background behind the
          text. The "Uninstall" button only appears if an existing install is
          detected on the system
        </p>

        <img
          src="https://wincent.com/a/products/install/screenshots/license.jpg"
          alt="License step"
          width="641"
          height="448"
        />
        <p>An example "License" phase.</p>

        <img
          src="https://wincent.com/a/products/install/screenshots/domain.jpg"
          alt="Choose Domain step"
          width="641"
          height="448"
        />
        <p>
          A "Domain" phase, where the user choose whether the software will be
          installed in the "local" domain (the user's home directory) or the
          "global" domain (a system-wide installation).
        </p>

        <img
          src="https://wincent.com/a/products/install/screenshots/copyFiles.jpg"
          alt="Copy Files step"
          width="641"
          height="448"
        />
        <p>
          Multi-threading in the "Copy Files" phase ensures that the user
          interface is responsive at all times during the install. Attention to
          detail and a high degree of user interface polish ensures that little
          things, like the ghosting of the "Go Back" and "Install" buttons
          during the operation, provide a robust, consistent user experience
          with no scope for accidental cancellations.
        </p>

        <img
          src="https://wincent.com/a/products/install/screenshots/finish.jpg"
          alt="Finish step"
          width="641"
          height="448"
        />
        <p>
          A "Finish" step, again easily customized and localized with an RTF
          file, in which the user can quit the Install application, launch the
          newly-installed product, or access its preferences.
        </p>

        <img
          src="https://wincent.com/a/products/install/screenshots/uninstall.jpg"
          alt="Uninstall step"
          width="641"
          height="448"
        />
        <p>
          With only a few additional lines in your XML property list
          configuration file, Install will be able to handle uninstallations for
          you as well. The "Uninstall" window allows users to choose exactly
          what gets removed, and it also allows you to determine which items
          will be "checked" by default. It is clever enough to only show files
          and folders which actually exist on the user's system.
        </p>

        <img
          src="https://wincent.com/a/products/install/screenshots/log.jpg"
          alt="Log window"
          width="591"
          height="345"
        />
        <p>
          The printable, saveable log window updates in real time and allows the
          curious user to see exactly what's going on behind the scenes. In the
          above log window you can see how Install has quit target processes,
          removed a stale cache file and a previous installation, and finally
          copied the new software into place.
        </p>

        <img
          src="https://wincent.com/a/products/install/screenshots/files.jpg"
          alt="Files window"
          width="381"
          height="264"
        />
        <p>
          The "Files" window allows users to see at a glance the files which
          will be installed.
        </p>

        <img
          src="https://wincent.com/a/products/install/screenshots/quit.jpg"
          alt="Quit dialog"
          width="448"
          height="213"
        />
        <p>
          Install makes sure that users don't accidentally exit during a
          sensitive operation, and provides a clearly worded dialog to help
          users decide the appropriate action.
        </p>
