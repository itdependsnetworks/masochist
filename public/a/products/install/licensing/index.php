          <h3><a name="brief" id="brief"></a>Licenses in brief</h3>

          <h4>Standard binary-only license</h4>

          <p>
            The standard, binary-only license starts at 199&euro;, and provides
            developers with all they need to package their own installer
            applications using the inbuilt feature set of the Install tool. Once
            you've purchased the license you can use Install to deploy any
            number of applications; there are no limits.
          </p>

          <p>
            Click
            <a href="https://wincent.com/a/products/install/purchase/"
              >here to purchase the binary-only license</a
            >; licenses are typically issued within 24 hours.
          </p>

          <p>
            It is important to note that the license grants the right to use
            Install to deploy (install) your software on customers' machines; it
            does not grant the right to re-sell, lease or otherwise offer
            Install itself as a product either independently or bundled with
            other software or material. See the
            <a href="standard-binary-license/">full license text</a> for more
            details.
          </p>

          <h4>Special binary-only license</h4>

          <p>
            This is a heavily discounted version of the binary-only license
            which costs only 29.95&euro;, and includes all the features and
            benefits of that license, but is intended only for use with freeware
            or open-source software projects. To qualify for this license the
            software to be deployed must be "freeware" (free of charge and
            completely functional without payment) or be licensed using an
            approved open-source license (the GNU GPL, GNU LGPL or BSD license).
            All other types of software, including "Shareware", "donationware"
            and commercial software, are not eligible for this license. Because
            terms like "freeware" and "donationware" are often used in different
            ways, please consult the <a href="../faq/">FAQ</a> or
            <a href="https://wincent.com/a/contact/">contact Wincent</a> if you
            are in doubt about whether your software would qualify.
          </p>

          <p>
            Click
            <a href="https://wincent.com/a/products/install/purchase/"
              >here to purchase the special binary-only license</a
            >; licenses are typically issued within 24 hours.
          </p>

          <p>
            See the <a href="special-binary-license/">full license text</a> for
            more details.
          </p>

          <h4>The Binary+framework license</h4>

          <p>
            A binary+framework license starts at 995&euro;, and includes all the
            material from the binary-only version, but also provides access to
            the framework that lies at the core of the Install application. With
            this framework, Cocoa developers can add their own custom subclasses
            to Install, tailoring and extending the tool to meet their exact
            needs.
          </p>

          <p>
            Click
            <a href="https://wincent.com/a/products/install/purchase/"
              >here to purchase the binary+framework license</a
            >; licenses are typically issued within 3 business days.
          </p>

          <p>
            It is important to note that the license grants the right to extend
            Install's functionality by leveraging the power of the framework; it
            does not grant the right to use the framework in other products
            whether derivative or non-derivative works, nor does it grant the
            right to sell, re-sell, lease or otherwise offer Install itself, or
            modified versions of Install, as a product either independently or
            bundled with other software or material. See the
            <a href="binary-plus-framework-license/">full license text</a> for
            more details.
          </p>

          <h4>The Full-source license</h4>

          <p>
            A full-source license starts at 4995&euro;, and offers the same
            benefits as the binary-only version, but also includes a complete
            copy of the source code, with full documentation in the headers. It
            is intended for serious developers who wish to use the Install code
            as a spring board for even more specialised flavours of the tool.
            With the full-source code license, you are limited only by your own
            creativity and expertise. Literally, the sky is the limit.
          </p>

          <p>
            Click
            <a href="https://wincent.com/a/products/install/purchase/"
              >here to purchase the full-source license</a
            >; licenses are typically issued within 7 business days.
          </p>

          <p>
            It is important to note that the license grants non-exclusive access
            to the Install source code for the purpose of extending and
