          <h3>Introducing Install</h3>
          <img
            style="float:left;"
            src="../gfx/install-icon.jpg"
            alt="Install icon"
            width="128"
            height="128"
          />
          <p>
            Install is the new and powerful software deployment solution that
            puts the robustness and security of Apple's own Installer
            application in your hands, without the hassle of wrestling with
            package files and installation scripts. Its modular design and
            flexible configuraton options make it easy to hand-tailor an
            installer to your exact requirements, and the finished product will
            immaculately conform to the Human Interface Guidelines that Mac
            users appreciate and expect.
          </p>
          <h4>Core features</h4>
          <p>
            Install brings you all the features you'd expect to find in a
            full-featured software deployment solution for Mac OS X:
          </p>
          <ul>
            <li>
              &quot;Pre-flight&quot;, graceful process termination (kill the
              processes you specify before commencing the installation, but do
              it gracefully, allowing users to save unsaved changes to disk
              where appropriate);
            </li>
            <li>
              Terminate target processes using Apple Events, SIGTERM signals, or
              SIGHUP signals;
            </li>
            <li>
              Automated pre-flight removal of files to the Trash (in the case of
              files which can be moved without administrator privileges); and
              pre-flight deletion of files (for files which require
              administrator privileges);
            </li>
            <li>Full configurability through a single XML property list;</li>
            <li>
              Total user interface customization made easy with RTF documents;
            </li>
            <li>
              Totally modular design allows you to make your installer as simple
              as you desire, or as complex; you control the number of steps
              required to complete the install;
            </li>
            <li>
              Easy-to-use &quot;Read Me&quot; steps, &quot;License&quot; steps,
              &quot;Domain&quot; steps (allowing the user to choose a local or a
              system-wide installation);
            </li>
            <li>
              &quot;Shell&quot; steps allow Install to run shell scripts before,
              after or during installation, with or without root privileges,
              thus enabling an unlimited range of tests and complex installation
              operations;
            </li>
            <li>
              &quot;Finish&quot; steps which allow the user to launch the
              installed application or open other documents (such as preferences
              or any other type of document);
            </li>
            <li>
              Authentication and authorization when required using Apple's
              Security frameworks;
            </li>
            <li>
              Graceful failure handling: if an error occurs during an install,
              users are provided with clear, easy-to-understand descriptions of
              what went wrong and how to fix the problem;
            </li>
            <li>Fully localizable;</li>
            <li>Saveable, printable log and file windows;</li>
            <li>
              &quot;Uninstall&quot; functionality, &quot;Upgrade&quot;
              functionality;
            </li>
            <li>
              &quot;Reveal files on disk&quot; menu command makes it convenient
              for users who wish to do manual (drag-and-drop) installation of
              files embedded inside the Install package;
            </li>
            <li>
              Multi-threaded to ensure interface responsiveness even during
              long-running installation processes;
            </li>
            <li>
              Target system version checking (Install itself is designed to run
              on Mac OS X 10.2 or later);
            </li>
            <li>
              Written in compact Objective-C, the Install application itself is
              barely over 200 kilobytes (including English localization
              resources);
            </li>
          </ul>
          <h4>Power meets ease-of-use</h4>
          <p>
            Install balances its impressive feature set with a carefully thought
            out, modular design that makes configuration a breeze.
          </p>
          <p>
            An attractive, flexible installer, updater and uninstaller
            application can be assembled in mere minutes thanks to the easy
            configuration of the XML property list that defines the application
            behaviour.
          </p>
          <p>
            Likewise, user interface elements (text, license agreements,
            documentation) are easily inserted with RTF documents.
          </p>
          <h4>Licensing options</h4>
          <p>
            Install is available in four licensing styles to suit the needs of
            different developers.
          </p>
          <p>
            The basic, <em>binary-only license</em> starts at 199&euro;, and
            provides developers with all they need to package their own
            installer applications using the inbuilt feature set of the Install
            tool. Once you've purchased the license you can use Install to
            deploy any number of applications; there are no limits.
          </p>
          <p>
            The <em>special binary-only license</em> is a discounted binary-only
            license for only 29.95&euro;, made available exclusively for use
            with open-source projects (that use the GPL, BSD or other approved
            license) or freeware products. To find out if you qualify for the
            discounted license see the <a href="licensing/">licensing page</a>.
          </p>
          <p>
            A <em>binary+framework</em> license starts at 995&euro;, and
            includes all the material from the binary-only version, but also
            provides access to the framework that lies at the core of the
            Install application. With this framework, Cocoa developers can add
            their own custom subclasses to Install, tailoring and extending the
            tool to meet their exact needs.
          </p>
          <p>
            A <em>full-source license</em> starts at 4995&euro;, and offers the
            same benefits as the binary-only version, but also includes a
            complete copy of the source code, with full documentation in the
            headers. It is intended for serious developers who wish to use the
            Install code as a spring board for even more specialised flavours of
            the tool.
          </p>
