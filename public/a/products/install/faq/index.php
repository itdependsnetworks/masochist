          <h4>
            <a id="credit" name="credit"></a>What credit do I have to give to
            Wincent if I use Install to deploy my own product(s)?
          </h4>

          <p>
            You may not strip any copyright notices from Install, regardless of
            the type of license that you enter into. The Install software
            itself, both in binary and source code form, is Copyright &copy; by
            Wincent Colaiuta, 2003-2004, All rights reserved. You retain the
            authorship of and copyright to any permitted modifications that you
            make to Install (exactly what modifications are permitted varies in
            each license, so please consult the full license text for details).
          </p>

          <p>What this means in practical terms is that:</p>

          <ul>
            <li>
              you may rename the Install application bundle itself (for example,
              you may wish to rename it from &quot;Install.app&quot; to
              &quot;Install Our Product Name.app&quot;);
            </li>
            <li>
              if you choose to rename the bundle you are not obliged to maintain
              the word &quot;Install&quot; in the title;
            </li>
            <li>
              you may add your own Copyright attribution to the
              CFBundleGetInfoString and NSHumanReadableCopyright fields in the
