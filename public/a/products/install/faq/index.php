          <h4>
            <a id="per-developer" name="per-developer"></a>Is the license fee
            per-product or per-developer? In other words, can I purchase one
            Install license and then use the product to deploy all of my
            company's software (multiple products)?
          </h4>

          <p>
            The license fee is &quot;per-vendor&quot;. If your company sells 20
            different Mac OS X software products, you can purchase a single
            Install license and you will be granted the right to use Install to
            deploy as many of those products as you wish.
          </p>

          <p>
            The definition of &quot;the vendor&quot; here will vary from
            customer to customer. If you are a lone developer working
            independently then you are the vendor. If your company purchases an
            install license, entering into the license agreement in the name of
            the company, then the company itself (and not you) is considered to
            be the licensee and therefore &quot;the vendor&quot;. When used with
            opensource projects then the collective that works on the project is
            considered to be &quot;the vendor&quot;.
          </p>

          <p>
            Licenses are not transferrable, so if you enter into a license
            agreement as an individual developer and then later move to another
            company, then you may not transfer your license to the new company
            and use Install to deploy that company's products under the terms of
            your previous license. Similarly, if an open source project acquires
            an Install license and the project later forks then the forked
            project would need to obtain its own Install license (because
            &quot;the vendor&quot; would be different); in order to avoid
            acquiring another license you would need to demonstrate that the
            vendor was effectively unchanged (in other words, that the project
            has the same development team and affiliated organization).
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a id="freeware" name="freeware"></a>What is the definition of
            "freeware" and "open source" for the purposes of the special
            binary-only license?
          </h4>

          <p>
            In order to qualify for the discounted binary-only license, the
            software to be deployed must be either "freeware" or "open source".
            Software is considered to be freeware if it is available
            <em>free of charge</em>, and it has full,
            <em>unchanged functionality</em> regardless of whether payment is
            made. Most software that is labelled as "shareware", "nagware",
            "donationware" or "commercial" does not satisfy this requirement.
          </p>

          <p>
            Software which displays a reminder dialog asking for payments, even
            if it claims to be "free", is not considered to be freeware for the
            purposes of licensing Install, because the reminder dialog qualifies
            as <em>changed functionality</em> (those who donate don't see the
            dialog, those who don't donate do). If you are in any doubt about
            whether your software would qualify, please
            <a href="https://wincent.com/a/contact/">contact Wincent</a>.
          </p>

          <p>
            Software is considered to be "open source" if it is licensed under
            one of the approved licenses, the GNU GPL, GNU LGPL or the BSD
            license.
          </p>

