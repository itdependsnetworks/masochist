              More sophisticated target version-checking allows Install to use
              different installation instructions for different versions of Mac
              OS X, or display a dialog if used to attempt to install software
              on an incompatible version of the operating system;
            </li>
            <li>
              New &quot;Shell script&quot; step allows Install to run shell
              scripts as part of the installation process (useful for
              pre-flight/post-flight checks or more complex, custom installation
              tasks);
            </li>
            <li>
              In addition, the &quot;Copy files&quot;, &quot;Finish&quot; and
              &quot;Uninstall&quot; steps can now run shell scripts;
            </li>
            <li>
              Install can now add or remove login items (from within the
              &quot;Copy files&quot; and &quot;Uninstall&quot; steps);
            </li>
            <li>
              In the event of an error while running an operation with root
              privileges, Install now logs more detailed information about the
              problem;
            </li>
            <li>
              Install's application menu now contains an item for going to the
              Install website;
            </li>
            <li>
              Cleaned up the user interface by disabling the contextual menu
              when clicking certain types of text; it is still possible to
              select text and use a contextual menu when looking at
              &quot;ReadMe&quot; files or licenses;
            </li>
