          <h3>Version history</h3>

          <h4><a id="1.3.1" name="1.3.1"></a>Version 1.3.1 (30 June 2007)</h4>

          <ul>
            <li>
              [Enhancement] Allow one-step installs (see the "What's New"
              documentation for more information on how this feature can be
              exploited to provide a better user experience).
            </li>
            <li>
              [Enhancement] To increase awareness that Install is also actually
              an uninstaller, show the "Uninstall" button as ghosted rather than
              hiding it completely when Install is first launched (<a
                href="https://wincent.dev/a/support/bugs/show_bug.cgi?id=579"
                >feature request #579</a
              >, "Display Uninstall button even if no previous install
              detected").
            </li>
            <li>
              [Enhancement] When resizing buttons to smaller sizes instruct the
              superview to redraw in that area also. The previous behaviour was
              not known to be defective, but this change should proof the code
              against possible changes in future versions of Mac OS X.
            </li>
            <li>
              [Bugfix] Workaround Cocoa bug in which the
              <tt>sizeToFit</tt> produced buttons that were marginally too
              narrow.
            </li>
            <li>
              [Note] Now ships with unstripped nib files; at the cost of a small
              increase in download size developers can now edit the nib files in
              Interface Builder.
            </li>
          </ul>

          <h4><a id="1.3" name="1.3"></a>Version 1.3 (22 November 2005)</h4>

          <ul>
            <li>
              If an error occurs while trying to move files to the Trash without
              administrator privileges Install now shows a dialog allowing the
              user to enter an administrator password and try again, reveal the
              files in the Finder for manual deletion, or cancel the operation.
            </li>
            <li>
              Added missing tilde (~) in the default configuration file which
              prevented developers from accessing the Install manual when
              pressing the "Documentation" button after installing for the
              current user only.
            </li>
            <li>Fixed minor memory leak (less than one kilobyte per run).</li>
            <li>
              Tested and qualified for use with Mac OS X 10.4, 10.4.1, 10.4.2,
              10.4.3 Tiger; Install continues to be compatible with Jaguar and
              Panther also.
            </li>
            <li>
              Built using Xcode 2.2 as a Universal Binary, which means that
              Install will run natively on both PowerPC and Intel processors.
              The PowerPC build now explicitly uses the 10.2 SDK which among
              other things forces GCC 3.3 to be used as the compiler and ensures
              that 10.2-compatible code is produced.
            </li>
          </ul>

          <h4>
            <a id="1.2.3" name="1.2.3"></a>Version 1.2.3 (9 December 2004)
          </h4>

          <ul>
            <li>
              Added additional checks in the deletion with root privileges
              routine. There was a very rare crash that could occur here on
              systems where the "/bin/rm" tool was not present. Fortunately the
              exact circumstances which provoke the crash are extremely
              uncommon; there has only been one crash report in 18 months. The
              additional checking ensures that Install will fail gracefully in
              this case rather than crash.
            </li>
            <li>Built using the latest version of Apple's Xcode Tools;</li>
            <li>
              Introduced new <a href="../licensing/">discount licensing</a> for
              use with freeware and open source products.
            </li>
          </ul>

          <h4><a id="1.2.2" name="1.2.2"></a>Version 1.2.2 (28 June 2004)</h4>

          <ul>
            <li>
              Rather than providing localized variants of the configuration
              file, developers now have the option of putting user-visible
              strings from the file into a separate
              &quot;InstallConfig.strings&quot; file, simplifying the task of
              localization for translators;
            </li>
            <li>
              More conservative removal of old files when upgrading Install
              itself.
            </li>
          </ul>

          <h4><a id="1.2.1" name="1.2.1"></a>Version 1.2.1 (23 June 2004)</h4>

          <ul>
            <li>
              A repackaged release issued because some of the improvements in
              recent versions of Install were not propagated through to the
              versions distributed to developers;
            </li>
            <li>
              On running a &quot;naked&quot; (unconfigured) installer, now
              displays a dialog before exiting;
            </li>
            <li>Recompiled with the latest version of Apple's Xcode Tools.</li>
          </ul>

          <h4><a id="1.2" name="1.2"></a>Version 1.2 (1 April 2004)</h4>

          <ul>
            <li>
              Can now specify to default to the Global installation domain
              (useful when omitting the &quot;Domain&quot; step and creating
              all-user-only installers);
            </li>
            <li>
              Fixed bug preventing user from cancelling an uninstall/install
              after failing to enter a correct administrator username and
              password.
            </li>
          </ul>

          <h4><a id="1.1.2" name="1.1.2"></a>Version 1.1.2 (29 March 2004)</h4>

          <ul>
            <li>
              Developers may now use the extended error-handling format in
              &quot;CopyFiles&quot;, &quot;Finish&quot; and
              &quot;Uninstall&quot; steps, which was previously only available
              in &quot;Shell&quot; steps;
            </li>
            <li>
              Developers wanting to make shorter installers can now omit the
              &quot;ReadMe&quot; step and use a &quot;License&quot;,
              &quot;Shell&quot;, &quot;Domain&quot; or &quot;CopyFiles&quot;
