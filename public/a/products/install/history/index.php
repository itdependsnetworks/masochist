          <h4><a id="1.1.1" name="1.1.1"></a>Version 1.1.1 (6 March 2004)</h4>

          <ul>
            <li>
              When removing login items developers may now use item names
              without paths, or full path names;
            </li>
            <li>
              When adding or removing login items developers may specify whether
              any given item should be added/removed if only installing for all
              users, or if only installing for the current user;
            </li>
            <li>
              More control over the response to &quot;Shell&quot; step exit
              codes; developers can now classify exit codes as
              &quot;Success&quot;, &quot;Non-fatal&quot;, &quot;Can retry&quot;
              and &quot;Fatal&quot;;
            </li>
            <li>
              If developers specify long button labels in their
              &quot;Finish&quot; step, the buttons automatically resize to
              accommodate the text;
            </li>
            <li>
              Fixed bug where users would still be asked for an administrator
              user name and password even though they were only trying to
              uninstall items which did not require elevated privileges;
            </li>
            <li>
              Developers may now optionally specify an RTF or RTFD document to
              customize the appearance of the &quot;Domain&quot; step;
            </li>
            <li>Updated Spanish, German and Japanese localizations.</li>
          </ul>

