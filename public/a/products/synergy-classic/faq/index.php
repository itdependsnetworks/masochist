          <h4>
            <a name="buttonGraphics" id="buttonGraphics"></a>How can I make my
            own menu bar button graphics?
          </h4>

          <p>
            If you would like to create your own button graphics and submit them
            for inclusion in <a href="../buttons/">the gallery</a> you can do so
            by preparing the graphics according to the following guidelines and
            then submitting them to me via email ("win" at this domain).
          </p>

          <ul>
            <li>
              Create a folder based on the name of your button set, plus a
              "synergyButtons" extension. By using this extension, the folder
              can be double-clicked and Synergy will automatically display the
              button set;
            </li>
            <li>
              Button graphics should be in PNG format, with transparent
              backgrounds so that the menu bar can show through any unused area;
            </li>
            <li>
              Two buttons of equal width (for visual symmetry) should be created
              for the &quot;next track&quot; and &quot;previous track&quot;
              buttons; these files must be named &quot;nextImage.png&quot; and
              &quot;prevImage.png&quot; respectively;
            </li>
            <li>
              Four more buttons (all with the same width as each other) should
              be created and named &quot;stopImage.png&quot;,
              &quot;playImage.png&quot;, &quot;pauseImage.png&quot; and
              &quot;playPauseImage.png&quot; (this last graphic is used when
              communication between Synergy and iTunes is interrupted and hence
              Synergy does not know whether to display a pause or a play image);
            </li>
            <li>
              All button graphics should be 22 pixels high (the height of the
              Mac OS X menu bar);
            </li>
            <li>
              Button graphics should be saved in &quot;web ready&quot; format
              (without resource forks);
            </li>
            <li>
              You should ensure that all graphics have a resolution of 72 DPI x
              72 DPI (dots per inch) -- the Mac OS X default -- otherwise Cocoa
              will not display them as expected in Panther;
            </li>
            <li>
              You should download and edit
              <a href="../buttons/Info.plist">this plist file</a> so as to
              include your contact details. The plist file contains information
              about you and/or your company that you may wish to provide, and
              because it is in a machine readable format Synergy Advance will be
              able to display that information in the user interface. Do not
              change the name of the file (it should remain as "Info.plist");
            </li>
            <li>
              Place all the button graphics and the Info.plist file inside your
              synergyButtons folder.
            </li>
          </ul>

          <p>
            Please note that a common error is incorporating blank space inside
            your button graphics in order to give them a "pleasing" spacing.
            This is what the "Button spacing" slider in the Synergy
            <a href="../preferences/menu-bar/">Menu Bar preferences</a> is for.
            By incorporating blank space inside your graphics, you make it
            impossible for users who prefer a tighter configuration to make full
            use of the slider. If you think your buttons look better spread out,
            then use the slider; do not build blank space into the button
            graphics themselves.
          </p>

