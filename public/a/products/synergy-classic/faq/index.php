          <h4>
            <a name="codeTek" id="codeTek"></a>How do I prevent CodeTek's
            VirtualDesktop application from interfering with the Synergy
            Floater?
          </h4>

          <p>
            <strong>Either</strong>,
            <a href="http://www.codetek.com/php/virtual.php"
              >upgrade your copy of VirtualDesktop to version 2.3.2 or later</a
            >.
          </p>

          <p>
            <strong>Or</strong>, if using versions of VirtualDesktop prior to
            version 2.3.2:
          </p>

          <ol>
            <li>
              Locate your installed copy of the Synergy.prefPane in the Finder.
            </li>
            <li>
              Right-click or Command+click the Synergy.prefPane and choose
              &quot;Show package contents&quot;.
            </li>
            <li>
              In the window that appears, open the &quot;Contents&quot; folder,
              and inside that, the &quot;Resources&quot; folder; you should see
              an application called &quot;Synergy&quot; among the other files.
            </li>
            <li>
              Now in the CodeTek VirtualDesktop preferences window, go to the
              &quot;Applications&quot; tab.
            </li>
            <li>
              Drag the Synergy application icon that you just located to the
              list of applications in VirtualDesktop.
            </li>
            <li>
              In VirtualDesktop, select the Synergy application from the list
              and activate the checkbox which says, &quot;Show this
              application's windows on all desktops&quot;.
            </li>
          </ol>

