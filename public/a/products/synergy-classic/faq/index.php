          </h4>

          <p>
            Hot Keys are based on hardware-dependent codes that vary from
            computer to computer. Although the default Hot Key settings that
            ship with Synergy will work on most Apple-branded equipment, there
            are some third-party keyboards and particular Apple models (such as
            certain iBooks) which will not respond properly to the default key
