            <a name="cache" id="cache"></a>Why does Synergy still appear in the
            System Preferences even after dragging it to the trash?
          </h4>

          <p>
            This is caused by a bug in Apple's System Preferences application
            which causes stale information to remain in the cache. Instructions
            for getting around the bug appear on the
            <a href="../installing/">installation and removal page</a>.
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a name="F14F15" id="F14F15"></a>How can I use F14 and F15 as Hot
            Keys?
          </h4>

          <p>
            It is not possible to set F14 and F15 for Hot Keys as Apple reserves
            them for controlling the brightness on their LCD displays. By adding
            a modifier to the Hot Key, such as &quot;Command&quot;, you may use
            those as Hot Keys, but you will not be able to use them for this
            purpose <i>without</i> a modifier unless Apple changes its
            implementation.
          </p>

