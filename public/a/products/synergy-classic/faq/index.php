            directly from your hardware and thus work with your keyboard even if
            it is a special or third-party model.
          </p>

          <p>
            Another situation in which Hot Keys may not work is if you choose a
            Hot Key combination that is already in use by another application.
            This is not recommended because the other application may
            &quot;capture&quot; those Hot Key presses and prevent them from ever
            reaching Synergy. Conversely, you may trigger an action in that
            application when you only intended to issue a directive to Synergy.
            As such it is recommended that you choose unique Hot Key
            combinations. (See also &quot;<a href="#functionKeysOnly"
              >Can I use just a function key as a Hot Key, and not have to use a
              modifier key as well?</a
            >&quot;)
