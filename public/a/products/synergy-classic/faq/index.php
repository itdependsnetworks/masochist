          <h4>
            <a name="versioncheck" id="versioncheck"></a>How do I disable or
            modify Synergy's automatic version checking feature?
          </h4>

          <p>
            By default, Synergy automatically checks once per day to see if a
            new version has been released. Your privacy is protected because no
            information is transmitted to the server in order to perform this
            check; Synergy merely asks the server what the latest version number
            is, and if the number is higher than the currently running version,
            a dialog is displayed.
          </p>

          <p>
            There is not yet a control in the user interface for this feature,
            but you can still control it by editing the Synergy preferences
            file. Make sure Synergy is not running when you edit the file. The
            file is at
            &quot;-/Library/Preferences/org.wincent.Synergy.plist&quot;. To
            modify the settings look for the following entry in the file:
          </p>

          <pre>
&lt;key&gt;Automatic Internet version checking interval&lt;/key&gt;
&lt;real&gt;86400&lt;/real&gt;</pre
          >

          <p>
            Here the number 86400 indicates that checking will occur once every
            86,400 seconds (in other words, once every day). If you set the
            number to 0 (zero) then checking will never occur; this is how you
            would disable the automatic version checking feature. The minimum
            value that you can use is 86400; lower values will be ignored and
            Synergy will check once per day.
          </p>

          <p>
            If you wish to check once per week you would use a value of 604800.
            If you wish to check once per month you would use a value of
            18396000. And if you wish to check once per year then you would use
            a value of 31536000.
          </p>

          <p>
            You must be careful when modifying the preferences file to change
            only the number and not any of the surrounding text. Doing so may
            result in a badly formed preferences file that Synergy cannot read.
          </p>

