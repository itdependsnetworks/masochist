          <h4>
            <a name="immediateHide" id="immediateHide"></a>Why don't the Synergy
            menu bar controls hide immediately when I quit iTunes?
          </h4>

          <p>
            This is not a bug, but a consequence of a design decision. One of
            Synergy's design goals is to be minimise load on system resources,
            and so it reduces CPU usage by only querying the status of iTunes
            once every 3 seconds.
          </p>

          <p>
            As a result, if you have checked the box, &quot;Display controls
            only when iTunes is running&quot; in the
            <a href=".../preferences/menu-bar/">Menu bar settings tab</a> you
            may find that there is sometimes a slight delay between iTunes
            launching or quitting, and Synergy's menu bar controls changing in
            response.
          </p>

          <p>
            A design goal for later versions of Synergy is to improve the
            efficiency of communication between iTunes and Synergy so as to
            permit more frequent querying, most likely around once per second,
            and therefore greater responsiveness.
          </p>

