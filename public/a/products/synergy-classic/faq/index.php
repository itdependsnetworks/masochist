          <h4>
            <a name="NSMenuExtra" id="NSMenuExtra"></a>Why can't I drag the
            Synergy controls around the menu bar when holding down the Command
            key?
          </h4>

          <p>
            Only applications that access Apple's private &quot;MenuExtra&quot;
            API can be moved in this way, and with the release of Mac OS X 10.2
            Apple removed the ability of third-party applications to access this
            undocumented API.
          </p>

          <p>
            There are applications on the market that hack their way around this
            restriction, but their techniques are not supported by Apple and
            there is no guarantee that they will continue to work in future
            versions of the OS.
          </p>

          <p>
            As such, Synergy elects not to make use of these methods, but
            instead uses the Apple-approved mechanism for appearing in the menu
            bar: the &quot;StatusBar&quot; API. This mechanism is officially
            supported by Apple. If and when Apple decides to allow StatusBar
            items to respond to Command-drag events, Synergy will be movable in
            this fashion.
          </p>

          <p>
            One benefit of using the StatusBar mechanism is that, unlike
            MenuExtras, StatusBar items do not crash when the SystemUIServer
            goes down (or is brought down by another MenuExtra). StatusBar items
            like Synergy cannot be crashed by, or cause to fail other items on
            the menu bar.
          </p>

