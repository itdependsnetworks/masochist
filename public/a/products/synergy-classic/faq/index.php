          <h4>
            <a name="playpause" id="playpause"></a>Why does the Synergy
            play/pause button change appearance?
          </h4>

          <p>
            Most of the time, the Synergy menu bar play/pause button will
            display either a &quot;play&quot; image (a right-pointing triangle)
            or a &quot;pause&quot; image (two vertical rectangles). At times,
            for example when dragging the iTunes window, the Synergy play/pause
            button may display a mixed &quot;play/pause&quot; image (one
            vertical rectangle and one right-pointing triangle). This occurs
            whenever the communications link between Synergy and iTunes is
            interrupted, and the combined &quot;play/pause&quot; image reflects
            the fact that the status of iTunes (playing or paused) is unknown to
            Synergy.
          </p>

          <p>
            One example of a situation in which this can occur is when the user
            drags the iTunes window around the screen, or runs an iTunes
            visualiser. Under these circumstances, because of the way it is
            programmed, iTunes does not respond to communication attempts made
            by other applications, such as Synergy. So in these cases, you might
            see the combined play/pause image; when communications are
            re-established and the state of iTunes is again known, the button
            will revert to the appropriate state.
          </p>

