            >
          </p>

          <h4>Please see the new Synergy product pages</h4>
          <p>
            Please note that the new Synergy product pages are now online at
            <a href="https://wincent.com/products/synergy"
              >https://wincent.com/products/synergy</a
            >. The page you are currently viewing will no longer be updated and
            is being kept online for historical purposes only.
          </p>

          <h4><a id="4.0.1a" name="4.0.1a"></a>4.0.1a (1 September 2009)</h4>

          <ul>
            <li>Require Mac OS X 10.6 Snow Leopard or above.</li>
            <li>
              Remove "Unregister" button from user interface to avoid unintended
              unregistrations.
            </li>
            <li>
              Provide means of entering new serial number when upgrading from a
              very old license.
            </li>
            <li>Remove old code paths to reduce disk and memory footprint.</li>
            <li>
              Remove duplicate classes across different bundles, reducing disk
              and memory footprint as well as silencing some console warnings.
            </li>
            <li>
              Eliminate all Interface Builder warnings about geometry (clipped
              views) and inappropriate settings.
