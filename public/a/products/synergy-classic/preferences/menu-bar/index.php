          <h3>Menu Bar preferences</h3>

          <p>
            You use the Menu Bar tab for controlling the appearance of Synergy
            in the Menu Bar; this includes adjusting the control buttons, and
            setting preferences for the Synergy Global Menu.
          </p>

          <ul>
            <li>
              <strong>Menu Bar control buttons</strong>:
              <ul>
                <li>
                  <strong>Play/Pause track</strong>: Check or uncheck this box
                  to enable and disable the "Play/Pause" Menu Bar control
                  button. The preview on the right-hand side will show you what
                  your menu bar controls will look like.
                </li>
                <li>
                  <strong>Previous track</strong>: Check or uncheck this box to
                  enable and disable the "Previous track" Menu Bar control
                  button. The preview on the right-hand side will show you what
                  your menu bar controls will look like.
                </li>
                <li>
                  <strong>Next track</strong>: Check or uncheck this box to
                  enable and disable the "Next track" Menu Bar control button.
                  The preview on the right-hand side will show you what your
                  menu bar controls will look like.
                </li>
                <li>
                  <strong>Display controls only when iTunes is running</strong>:
                  When this box is checked, Synergy will automatically show its
                  control buttons whenever iTunes is launched, and hide its
                  control buttons whenever iTunes is quit. There may be a delay
                  of up to 10 seconds before Synergy is notified of an iTunes
                  launch or quit.
                </li>
                <li>
                  <strong>Button style</strong>: You can change the visual style
                  of the menu bar control buttons by choosing from this pop-up
                  menu. If you wish to create your own button graphics you
                  should read the information on the
                  <a href="../../faq/">Frequently Asked Questions page</a>. You
                  can also click the "More..." button to visit the
                  <a href="../../buttons/">free, online gallery</a> of
                  contributed button sets.
                </li>
                <li>
                  <strong>Button spacing</strong>: You can adjust this slider to
                  control the spacing between the Synergy control buttons; the
                  preview will update to show you what the menu bar controls
                  will look like.
                </li>
              </ul>
            </li>
            <li>
              <strong>Global Menu</strong>: The Global Menu provides access to
              additional Synergy features and options not available from iTunes'
              own Dock menu. You can access it at any time by using the mouse to
              click-and-hold the Play/Pause button in the menu bar (or, if the
              menu bar controls are not displayed, Synergy appears as a musical
              note in the menu bar which you can access like a normal menu bar
              item).
              <ul>
                <li>
                  <strong>Include Recently Played Tracks in Menu</strong>: When
                  this box is checked the Synergy Global Menu will include a
                  list of recently played tracks which you can play again by
                  choosing them from the list. Duplicates are automatically
                  removed from the menu.
                  <ul>
                    <li>
                      <strong>Include artist information if available</strong>:
                      When checked, the Synergy Global Menu will include artist
                      information in addition to track names, if available.
                    </li>
                    <li>
                      <strong>Remember</strong>: This pop-up allows you to alter
                      the maximum number of recently played tracks that will
                      appear in the Menu.
                    </li>
                  </ul>
                </li>
                <li>
                  <strong>Include Playlist Submenu</strong>: When enabled, a
                  playlist submenu appears containing all of your custom
                  playlists. You can switch to a playlist and start playing it
                  by choosing it from the submenu (by default, iTunes will not
                  come to the front when switching playlists, but you can change
                  this behaviour in the Advanced tab of the Synergy
                  preferences).
                </li>
                <li>
                  <strong>Include iTunes Submenu</strong>: When this box is
                  checked, the Synergy Global Menu will include a submenu for
                  launching and quitting iTunes, changing the shuffle and repeat
                  modes, and bringing iTunes to the front.
                </li>
              </ul>
            </li>
          </ul>
