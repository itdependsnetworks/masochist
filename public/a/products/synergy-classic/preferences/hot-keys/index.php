          <h3>Hot Key preferences</h3>

          <p>
            You use the Hot Keys tab to customise the Hot Key assignments that
            you will use to control iTunes and Synergy. It is recommended that
            you customise the settings, because the defaults are
            hardware-dependent and may not work on all keyboards. The available
            actions are listed in this tab, along with the Hot Key combination
            currently assigned to each action.
          </p>

          <ul>
            <li>
              <strong>Control iTunes and Synergy with global Hot Keys</strong>:
              when checked, you will be able to control Synergy by pressing
              "global Hot Keys"; these are key combinations of at least one
              modifier (Command, Control, Option, Shift) plus another key (for
              example, F1, F2 or F3). New in Synergy 0.6 is the ability to use
              raw Function Keys without any modifier. Global Hot Keys are useful
              for quickly and easily triggering actions when you hands are
              already on the keyboard and you do not want to move to the mouse.
            </li>
            <li>
              <strong>Play/Pause</strong>: This action sends a "Play/Pause"
              message to iTunes: if iTunes is not playing, it will start to
              play; if it is already playing, it will pause. In addition, if
              iTunes is not running, this action will cause iTunes to launch and
              start playing. Customise the Hot Key for this action by clicking
              the "Set..." button.
            </li>
            <li>
              <strong>Previous track</strong>: This action sends a "Previous
              track" message to iTunes. Customise the Hot Key for this action by
              clicking the "Set..." button.
            </li>
            <li>
              <strong>Next track</strong>: This action sends a "Next track"
              message to iTunes. Customise the Hot Key for this action by
              clicking the "Set..." button.
            </li>
            <li>
              <strong>Volume down</strong>: The reverse of the "Volume up"
              action.
            </li>
            <li>
              <strong>Volume up</strong>: This causes the iTunes volume (not the
              system volume) to increase; a semi-transparent feedback window
              will be displayed.
            </li>
            <li>
              <strong>Toggle mute</strong>: This causes iTunes to switch between
              muted and unmuted states.
            </li>
            <li>
              <strong>Toggle shuffle</strong>: This will toggle iTunes' shuffle
              mode on or off; a semi-transparent feedback window will be
              displayed to show the new shuffle status.
            </li>
            <li>
              <strong>Bring iTunes to front/hide iTunes</strong>: This will
              bring iTunes to the front; if iTunes is not running, it will be
              launched; if it is already running then it will be hidden. In this
              way you may bring iTunes to the front and then send it back again
              with two successive presses of the hot key combination.
            </li>
            <li>
              <strong>Show/hide Synergy Floater</strong>: Pressing this hot key
              brings the floater immediately to the front; it will fade out
              after a delay (which you can customise in the
              <a href="../general/">general settings tab</a>) or you can dismiss
              it immediately by pressing the hot key again.
            </li>
            <li>
              <strong>Rate as no stars</strong>: This sets the rating of the
              currently playing track to zero (no stars); a feedback window will
              be displayed confirming the change.
            </li>
            <li>
              <strong
                >Rate as 1 star, Rate as 2 stars, Rate as 3 stars, Rate as 4
                stars and Rate as 5 stars</strong
              >: These hot key combinations allow you to set the rating of the
              currently playing track. To fully benefit from the iTunes song
              rating system, it is recommended that you
              <a href="../general/">enable the display of ratings</a> in the
              Synergy floater.
            </li>
            <li>
              <strong>Set repeat mode</strong>: This causes iTunes to cycle
              through the available repeat modes: "one" (the current track is
              repeated), "all" (all the tracks in the current playlist are
              repeated), and "none" (no repeat).
            </li>
            <li>
              <strong>Show/hide Synergy menu buttons</strong>: This action
              causes Synergy to temporarily hide its menu bar control buttons,
              or to show them if they are already hidden. (It is also possible
              to have Synergy automatically show and hide its buttons, depending
              on whether iTunes is running; see the
              <a href="../menu-bar/">Menu Bar settings tab</a> below.) Customise
              the Hot Key for this action by clicking the "Set..." button.
            </li>
            <li>
              <strong>Quit Synergy</strong>: This action causes Synergy to quit.
              It will remove any control buttons from the menu bar, and the
              global menu (if enabled). In order to restart Synergy, you will
              need to use the Start/Stop button again in the
              <a href="../general/">General tab</a>. Customise the Hot Key for
              this action by clicking the "Set..." button.
            </li>
          </ul>

          <p>
            Whenever you click a "Set..." button to customise a Hot Key
            combination, a sheet will roll down that asks you to press a new
            combination.
          </p>

          <p>
            In general, your Hot Key combination must include at least one
            modifier key (Shift, Command, Option or Control). The exception to
            this rule is the Function Keys, which can be used without modifiers
            for even greater speed and convenience. Once you are have pressed
            your new combination, you can press "OK" to accept it or "Cancel" to
            dismiss the sheet without making any changes. The "Clear" button
            clears the Hot Key setting (you will see "(none)" appear in the
            sheet to indicate that there is no Hot Key setting in effect).
          </p>

          <p>
            If you choose a duplicate setting that is already in use by another
            Synergy action, then your new setting will take precedence and the
            other action will be cleared. This is because Synergy will not allow
            one Hot Key combination to trigger more than one action.
          </p>

          <p>
            You should be careful when choosing Hot Key combinations to avoid
            combinations that might be in use by other applications.
            Unpredictable results might ensue if you choose a Hot Key that
            conflicts with another application.
          </p>
