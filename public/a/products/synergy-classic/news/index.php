          <h3>Synergy news</h3>

          <p>
            <em
              >Confused by the version numbers? See
              <a
                href="https://typechecked.net/blog/clearing-up-confusion-about-synergy-version-numbers"
                >this blog post explaining the difference</a
              >
              between the 3.4 (stable) and 4.0 (alpha/experimental) series.</em
            >
          </p>
          <h5>1 September 2009</h5>

          <h4>New Synergy product pages</h4>

          <p>
            The Synergy product pages have been completely redesigned and can
            now be found at
            <a href="https://typechecked.net/products/synergy"
              >https://typechecked.net/products/synergy</a
            >. The page you are currently viewing will no longer be updated and
            is being kept online for historical interest only. For further news
            updates please see
            <a href="https://typechecked.net/blog">https://typechecked.net/blog</a>.
          </p>

          <h4>Synergy <a href="../history/#4.0.1a">4.0.1a</a> released</h4>

          <p>
            This is the new release in the alpha/experimental series, and is
            exclusively for Mac OS X Snow Leopard and later. The latest release
            in the stable series (3.4) runs on all versions of Mac OS X from
            10.2.8 Jaguar onwards, and was made available in late June. Both
            series are available for immediate download from the
            <a href="../download/">download page</a>.
