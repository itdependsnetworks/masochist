          <h3>Features</h3>

          <h4>
            <a id="half-stars" name="half-stars"></a>How do I set/view half-star
            ratings?
          </h4>

          <p>
            To <em>see</em> half-star ratings in iTunes (<a
              href="https://wincent.com/a/news/archives/2006/01/halfstar_rating.php"
              >as shown here</a
            >) you must use iTunes version 6.0.2 or later. If you use an older
            version of iTunes you can still <em>set</em> half-star ratings and
            iTunes will store them internally <em>but</em> it will always round
            them to whole stars for display in its user interface.
          </p>

          <p>
            To set half-star ratings using Synergy Advance you go to the
            <a
              href="https://wincent.com/a/products/synergy-advance/help/preferences/advanced/"
              >"Advanced" preferences</a
            >
            and set these two options:
          </p>

          <ul>
            <li>
              <a
                href="https://wincent.com/a/products/synergy-advance/help/preferences/advanced/#show-half-stars"
                >"Show half star ratings"</a
              >: (check)
            </li>
            <li>
              <a
                href="https://wincent.com/a/products/synergy-advance/help/preferences/advanced/#rating-step"
                >"Rating change step"</a
              >: (10)
            </li>
          </ul>

          <p>
            You can then use Synergy Advance's hot keys to raise and lower the
            ratings by half-star intervals (the rating change step of "10"
            corresponds to one half-star; the standard rating change step of
            "20" corresponds to whole stars). If you have
            <a
              href="https://wincent.com/a/products/synergy-advance/help/preferences/hot-keys/#hotkey-bezels"
              >"Extra visual feedback for Hot Keys"</a
            >
            checked in the
            <a
              href="https://wincent.com/a/products/synergy-advance/help/preferences/hot-keys/"
              >"Hot Keys" preferences</a
            >
            then Synergy Advance will display a bezel showing the current rating
            whenever you use the hot keys to change the rating.
          </p>

          <p>
            To see half-star ratings if you're using
            <a
              href="https://wincent.com/a/products/synergy-advance/help/preferences/growl/"
              >Growl</a
            >
            you must use Synergy Advance 0.3 or later.
          </p>
