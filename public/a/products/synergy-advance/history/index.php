          <h3>Version history</h3>

          <h4>
            <a id="0.5.1b" name="0.5.1b"></a>Public preview release version
            0.5.1b (17 May 2007):
          </h4>

          <ul>
            <li>[Feature] User-customizable Dock menu.</li>
            <li>
              [Feature] Five additional optional hot key slots for for assigning
              half-star ratings.
            </li>
            <li>
              [Feature] Ability to set half-star ratings by holding down the
              option key while navigating the "Rating" menu; this applies both
              to the Global Menu and the Dock menu (<a
                href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=240"
                >feature request #240</a
              >, "Menu items for half-star ratings").
            </li>
            <li>
              [Enhancement] Eliminate some redundant code only used on Panther
              for better performance.
            </li>
            <li>
              [Bugfix] For the purposes of updating the Global Menu, the "Fast
              forwarding" and "Rewinding" states are now considered to be
              functionally equivalent of the "Playing" state; previously they
              were left to fallback to the default (unknown) state.
            </li>
            <li>
              [Bugfix] To avoid confusion, suppressed echoing of harmless
              warning messages to console at default log level (<a
                href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=568"
                >bug #568</a
              >, AEGetParamPtr error message written to Console every two
              seconds").
            </li>
          </ul>

          <h4>
            <a id="0.5b" name="0.5b"></a>Public preview release version 0.5b (26
            April 2007):
          </h4>

          <ul>
            <li>
              [Feature] It is now possible to provide overrides in the Instant
              Messaging preferences panel to be used depending on whether iTunes
              is playing, paused, stopped or not running (<a
                href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=471"
                >feature request #471</a
              >, "Separate control over iChat status when playing, paused,
              stopped etc").
            </li>
            <li>
              [Feature] Warning icon now shown when an invalid format string is
              entered; clicking on the icon takes the user to the appropriate
              help page on the website (<a
                href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=475"
                >feature request #475</a
              >, "Show warning icon in UI for invalid format strings").
            </li>
            <li>
              [Feature] Three new checkboxes added to the Growl preference pane,
              allowing users to determine exactly which state changes
              ("Playing", "Paused" or "Stopped") should trigger notifications
              (<a href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=527"
                >feature request #527</a
              >, "Option to turn off 'pause' notifications for Growl").
            </li>
            <li>
              [Feature] Many new Dock icon options for controlling opacity,
              colors, badging, progress meter; almost every aspect of the Dock
              icon can be customized.
            </li>
            <li>
              [Enhancement] Plug-ins can now add their own custom preference
              panes to the Synergy Advance preferences window. As an example,
              the iChat support is now bundled entirely within the iChat plug-in
              (previously it consisted of two separate components: a plug-in and
              a preference pane). This will enable the future deployment of
              fully-featured plug-ins that are well-integrated into the existing
              interface.
            </li>
            <li>
              [Enhancement] All Dock-related code has been moved into a separate
              plug-in which provides a dedicated preference pane for
              Dock-related preferences. Users not wishing to use Synergy
              Advance's Dock icon features can now deactivate the plug-in
              entirely for a performance and memory benefit.
            </li>
            <li>
              [Enhancement] Optimizations made to reduce the number of times
              Synergy Advance queries iTunes for cover art. Previously Synergy
              Advance queried for cover art every time a notification was
              received; now it only queries when there is a track change. As
              querying for cover art is a relatively resource-intensive
              operation this should lead to a more responsive user experience.
            </li>
            <li>
              [Enhancement] Application launch time is improved as a result of
              dismantling the large support framework, WOBase, and incorporating
              the code directly in the application itself. Likewise, absorbed
              the shared code used by all preference panes and plug-ins
              (previously in the "SynergyAdvance.framework") directly into the
              main application for further gains.
            </li>
            <li>
              [Enhancement] For ease of use the outline view in the Hot Keys
              preferences pane is automatically "expanded" when first displayed.
            </li>
            <li>
              [Enhancement] Massive code-audit and refactoring effort; almost
              every method in in every file has been improved and reworked under
              the rewrite (better logging of error conditions, more extensive
              exception handling, code more logically grouped for better
