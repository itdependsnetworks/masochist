          <h3>Synergy Advance support</h3>

          <p>
            The following support resources are available to you 24 hours a day,
            365 days a year through this website.
          </p>

          <h4>Synergy Advance Frequently Asked Questions</h4>

          <p>
            The <a href="../faq/">FAQ</a> contains the answers to the most
            common Synergy Advance questions.
          </p>

          <p class="more">Click <a href="../faq/">here</a> to view the FAQ</p>

          <h4>Synergy Advance help documentation</h4>

          <p>
            The <a href="../help/">help documentation</a> provides lots of
            information about what Synergy Advance is, how it works, what the
            different options mean and how to get it to do what you want.
          </p>

          <p class="more">
            Click <a href="../help/">here</a> to view the help documentation
          </p>

          <h4>Lost license codes</h4>

          <p>
            The
            <a href="https://wincent.com/a/support/registration/"
              >license code retrieval system</a
            >
            allows you to enter the email address you used at the time of
            purchase and have your license code re-mailed to that address.
            Licenses are tied to the purchase address for security reasons, so
            it is important to correctly specify the address when purchasing and
            to keep good backups in the event that you should change addresses.
            It is recommended that you burn a copy of your license notice to CD
            and also keep a printed copy.
          </p>

          <p class="more">
            Click
            <a href="https://wincent.com/a/support/registration/">here</a> to
            retrieve your code
          </p>

          <h4>Support pages</h4>

          <p>
            The
            <a href="https://wincent.com/a/support/"
              >wincent.com support pages</a
            >
            group together all the support resources available to you from this
            website, as well as containing site-wide support information that
            applies not only to Synergy Advance but to all other Wincent
            products and services.
          </p>

          <p class="more">
            Click <a href="https://wincent.com/a/support/">here</a> to go to the
            support pages
          </p>

          <h4>Support forums</h4>

          <p>
            The
            <a href="http://forums.wincent.com/"
              >support and discussion forums</a
            >
            provide you with a place where you can share your experiences and
            ask questions with other users at any hour of the day or night. The
            forums are equipped with a search function so that you can locate
            answers to previously asked questions.
          </p>

          <p class="more">
            Click <a href="http://forums.wincent.com/">here</a> to visit the
            forums
          </p>

          <h4>Bug tracking and feature requests database</h4>

          <p>
            You can use the
            <a href="http://bugs.wincent.com/"
              >bug tracking and feature requests database</a
            >
            to submit bug reports and feature requests, as well as see what
            other users have reported and requested already.
          </p>

          <p class="more">
            Click <a href="http://bugs.wincent.com/">here</a> to visit the
            database
          </p>

          <h4>Mailing lists</h4>

          <p>There are two Synergy Advance mailing lists that you can join:</p>

          <ul>
            <li><em>announce</em>: Synergy Advance announcements list</li>
            <li><em>beta</em>: Synergy Advance beta testing list</li>
          </ul>

          <p>
            For more information, or to subscribe or unsubscribe, proceed to
            <a href="https://wincent.com/a/support/lists/"
              >the mailing lists page</a
            >.
          </p>

          <h4>Knowledge base</h4>

          <p>
            The wincent.com
