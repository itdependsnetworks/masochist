          <h3>Synergy Advance help: hot keys</h3>

          <p>
            Synergy Advance features a powerful and flexible hot key engine that
            allows you to define system-wide global hot key combinations for a
            large number of functions. By pressing the desired hot key
            combination you can trigger an action regardless of the active or
            frontmost application. This enables you to control your audio
            without interrupting your workflow.
          </p>

          <p>
            To use hot keys make sure the "Enable Hot Keys" checkbox in the Hot
            Keys preferences pane of Synergy Advance is activated. If desired
            you can also limit hot keys so that they only operate while iTunes
            is running by checking the "Only when iTunes is running" checkbox.
          </p>

          <p>
            The system allows you to select almost any combination of normal
            keys (for example, letter keys, number keys, function keys and so
            forth) plus zero or more modifier keys (allowed modifier keys are
            Shift, Control, Option, Command, and the "fn" key that appears on
            most recent PowerBook keyboards). The modifier keys are optional but
            in most cases it is a good idea to use at least one modifier key to
            minimize clashes with other applications that try to use the same
            keys. For example, it would be unwise to set the "Space" key as a
            hot key without a modifier; if you did so then you would trigger the
            hot key's action every time you pressed the space bar! In the case
            of the function keys (F1, F2 and so forth) you may decide to set
            modifierless combinations for the sake of convenience, but be aware
            that applications often compete for these keys and there may be
            conflicts; for example, Apple by default assigns many function keys
            to Expos&eacute; functions, system volume control and screen
            brightness on some systems.
          </p>

          <h4>Limitations</h4>

          <p>
            There are some keys on the keyboard which cannot be used in hot key
            combinations because Mac OS X intercepts them at a very low level
            and does not pass them on to applications even when they are pressed
            in conjunction with modifier keys. These include the keys for
            increasing, decreasing or muting the system volume and the eject
            button which which appear on most recent Apple keyboards.
          </p>

          <p>
            You can define multiple hot keys for a single action (for example,
            you might decide that you want to use Command-F1
            <em>or</em> Shift-Command-Space to start playback) but you cannot
            use the same hot key to trigger multiple actions (for example, you
            cannot use Command-F1 to both raise the iTunes volume
            <em>and</em> skip to the next track). This limitation is in place to
            disallow contradictory definitions (for example, what would happen
            if the same key was supposed to both raise the rating and lower
            it?). If you try to define a hot key that is already in use for
            another action in Synergy Advance the previously defined hot key
            will be unassigned from the old action and reassigned to the new
            action. A warning is displayed in the user interface when this
            occurs.
          </p>

          <h4>Hot Keys options</h4>

          <h5>Enable Hot Keys</h5>

          <p>
            This checkbox is used to turn Synergy Advance's hot key support on
            and off.
          </p>

          <h5>Only when iTunes is running</h5>

          <p>
            You can further control whether the Synergy Advance hot keys should
            be active with this checkbox. When checked, Synergy Advance's hot
            keys will only be enabled if iTunes is running <em>and</em> you have
            checked the "Enable Hot Keys" checkbox. Otherwise hot key support
            will be inactive.
          </p>

          <h5>
            <a id="hotkey-bezels" name="hotkey-bezels"></a>Extra visual feedback
            for Hot Keys
          </h5>

          <p>
            You can use this checkbox to turn on additional visual feedback
            bezels when pressing selected hot keys. The feedback is displayed in
            the form of a semi-transparent bezel that matches the styling and
            appearance of Apple's bezels (which you see when you adjust the
            system volume, screen brightness, or turn muting on or off using the
            specially marked function keys on Apple keyboards). Affected hot
            keys include "Play/Pause", "Next", "Back", "Previous", "Increase
            rating", "Decrease rating", "Increase volume", "Decrease volume",
            "Toggle Shuffle", "Cycle Repeat Mode", "Toggle Muting", "Toggle
            Equalizer", "Skip forwards", "Skip backwards", "Enable/Disable Track
            in iTunes", "Play", "Pause", "Stop", "Fast Forward", "Rewind",
            "Resume", "Rate zero stars", "Rate one star", "Rate two stars",
            "Rate three stars", "Rate four stars", "Rate five stars", "Turn
            Shuffle On", "Turn Shuffle Off", "Repeat None", "Repeat All",
            "Repeat One", "Turn Muting On", "Turn Muting Off", "Turn Equalizer
            On", "Turn Equalizer Off", "Enable (check) Track in iTunes" and
            "Disable (uncheck) Track in iTunes".
          </p>

          <h4>iTunes controls</h4>

          <h5>Play/Pause</h5>

          <p>
            Tells iTunes to "Play/Pause". If already playing it will enter a
            paused state. If paused it will enter a playing state.
          </p>

          <p>
            If desired you can also use the
            <a href="../advanced/#avoid-playpause"
              >'Prefer "play" and "pause" over "playpause"'</a
            >
            setting in the "Advanced" pane of the Synergy Advance preferences to
            fine-tune the behaviour of this hot key. Basically, you can force
            Synergy Advance to send "play" and "pause" commands to iTunes rather
            than "playpause" commands, thus working around a bug in the iTunes
            "playpause" implementation. See the
            <a href="../advanced/"
              >documentation for the "Advanced" preferences</a
            >
            for more information.
          </p>

          <h5>Next</h5>

          <p>
            Tells iTunes to move to the next track in the current playlist. The
            exact behaviour of this action depends on what kind of playlist is
            current in iTunes and which track is currently selected. For
            example, in the case of Internet Radio stations performing the
            "Next" action will move to the next station in the list.
          </p>

          <p>
            This hot key differentiates between simple presses and
            press-and-hold actions. In the case of a simple press it behaves as
            already described. In the case of a press-and-hold it causes iTunes
            to fast forward within the current track. iTunes will continue to
            fast forward until you release the hot key or until the end of the
            track is reached, whichever comes first. Note that there are some
            types of track for which the fast forward action does not work
            (Internet Radio tracks, for example).
          </p>

          <h5>Back</h5>

          <p>
            Tells iTunes to move back. If already at the start of a track iTunes
            will try to move to the previous track. If midway through a track
            iTunes will move back to the start of the track. As is the case with
            the "Next" action the behaviour of this hot key depends on the
            playlist type and the currently selected track.
          </p>

          <p>
            This hot key differentiates between simple presses and
            press-and-hold actions. In the case of a simple press it behaves as
            already described. In the case of a press-and-hold it causes iTunes
            to rewind within the current track. iTunes will continue to rewind
            until you release the hot key or until the beginning of the track is
            reached, whichever comes first. Note that there are some types of
            track for which the rewind action does not work (Internet Radio
            tracks, for example).
          </p>

          <h5>Previous</h5>

          <p>Tells iTunes to play the previous track.</p>

          <p>
            This hot key differentiates between simple presses and
            press-and-hold actions. In the case of a simple press it behaves as
            already described. In the case of a press-and-hold it causes iTunes
            to rewind within the current track. iTunes will continue to rewind
            until you release the hot key or until the beginning of the track is
            reached, whichever comes first. Note that there are some types of
            track for which the rewind action does not work (Internet Radio
            tracks, for example).
          </p>

          <h5><a id="next-album" name="next-album"></a>Next album</h5>

          <p>
            Tells iTunes to skip forwards within the current playlist to the
            first track of the next album. Takes no action if there is no next
            album. In the current implementation only "file" tracks are
            considered (this means tracks that are physically located on your
            disk). Tracks stored elsewhere (such as on shared playlists, iPods,
            CDs or Internet radio tracks) are not considered while determing
            what is the next album.
          </p>

          <h5>Previous album</h5>

          <p>
            Tells iTunes to skip backwards within the current playlist to the
            first track of the previous album. Takes no action if there is no
            previous album. In the current implementation only "file" tracks are
            considered (this means tracks that are physically located on your
            disk). Tracks stored elsewhere (such as on shared playlists, iPods,
            CDs or Internet radio tracks) are not considered while determing
            what is the previous album.
          </p>

          <h5>
            <a id="increase-rating" name="increase-rating"></a>Increase rating
          </h5>

          <p>
            Tells iTunes to increase the rating of the current track. Note that
            there are some types of track for which iTunes will not accept
            ratings (for example, Internet Radio tracks). It is possible to use
            the <a href="../advanced/#rating-step">advanced preferences</a> to
            customize how much the rating should increase each time you press
            this hot key.
          </p>

          <p>
            This hot key differentiates between simple presses and
            press-and-hold actions. In the case of a simple press it behaves as
            already described. In the case of a press-and-hold it causes the
            rating to continue incrementing until you release the hot key. You
            can use the
            <a href="../advanced/#rating-delay">advanced preferences</a> to
            control the amount of time that should pass before the
            press-and-hold action should take effect.
          </p>

          <h5>Decrease rating</h5>

          <p>Tells iTunes to decrease the rating of the current track.</p>

          <p>
            This hot key differentiates between simple presses and
            press-and-hold actions. In the case of a simple press it behaves as
            already described. In the case of a press-and-hold it causes the
            rating to continue decrementing until you release the hot key. You
            can use the
            <a href="../advanced/#rating-delay">advanced preferences</a> to
            control the amount of time that should pass before the
            press-and-hold action should take effect.
          </p>

          <h5><a id="volume" name="volume"></a>Increase volume</h5>

          <p>
            Tells iTunes to increase its current playback volume. It is possible
            to use the
            <a href="../advanced/#volume-step">advanced preferences</a> to
            customize how much the volume should increase each time you press
            this hot key.
          </p>

          <p>
            This hot key differentiates between simple presses and
            press-and-hold actions. In the case of a simple press it behaves as
            already described. In the case of a press-and-hold it causes the
            volume to continue increasing until you release the hot key. You can
            use the
            <a href="../advanced/#volume-delay">advanced preferences</a> to
            control the amount of time that should pass before the
            press-and-hold action should take effect.
          </p>

          <h5>Decrease volume</h5>

          <p>Tells iTunes to decrease its current playback volume.</p>

          <p>
            This hot key differentiates between simple presses and
            press-and-hold actions. In the case of a simple press it behaves as
            already described. In the case of a press-and-hold it causes the
            volume to continue decreasing until you release the hot key. You can
            use the
            <a href="../advanced/#volume-delay">advanced preferences</a> to
            control the amount of time that should pass before the
            press-and-hold action should take effect.
          </p>

          <h5>Toggle Shuffle</h5>

          <p>
            Tells iTunes to toggle the current shuffle setting. If shuffle mode
            is currently turned on it will be turned off; if it is off it will
            be turned on. Note there are some types of playlist for which it is
            not possible to change the shuffle mode (for example, when listening
            to Internet radio).
          </p>

          <h5>Cycle Repeat Mode</h5>

          <p>
            Tells iTunes to cycle to the next repeat mode of the current
            playlist. By pressing this hot key repeatedly you can cycle through
            all three possible settings ("repeat off", "repeat all" and "repeat
            one").
          </p>

          <h5>Toggle Muting</h5>

          <p>
            Tells iTunes to toggle the current muting setting. If muting is
            currently activated it will be turned off; if it is deactivated it
            will be turned on.
          </p>

          <h5>Toggle Equalizer</h5>

          <p>
            Tells iTunes to toggle the equalizer from on to off, or from off to
            on.
          </p>

          <h5>Show/Equalizer Window</h5>

          <p>
            Tells iTunes to show the equalizer window if it is not currently
            showing; otherwise hides the equalizer window.
          </p>

          <h5>Toggle Visualizer</h5>

          <p>
            Tells iTunes to turn the visualizer on if it is currently off, and
            off if it is currently on.
          </p>

          <h5>Toggle Visualizer Fullscreen</h5>

          <p>
            Tells iTunes to enable fullscreen mode for the visualizer if it is
            not already enabled; otherwise it disables fullscreen mode. Note
            that this hot key does not turn the visualizer on or off; it merely
            sets whether it should use the fullscreen when activated.
          </p>

          <h5>Cycle Visualizer Size</h5>

          <p>
            Tells iTunes to cycle to the next visualizer size setting. By
            pressing this hot key repeatedly you can cycle the visualizer size
            through all three possible settings (small, medium and large). Note
            that this hot key does not turn the visualizer on or off; it merely
            sets the size used by the visualizer when activated.
          </p>

          <h5>Skip forwards</h5>

          <p>
            Tells iTunes to skip forwards within the current track by a
            predetermined number of seconds (the default is 3 seconds but you
            can change this in the
            <a href="../advanced/">advanced preferences</a>). If skipping
            forwards causes iTunes to come to the end of the current track then
            it will move to the beginning of the next track. There are some
            track types (such as Internet Radio streams) for which pressing this
            hot key will have no effect. The main purpose of this hot key
            combination is to assist people who use iTunes to perform
            transcription work.
          </p>

          <h5>Skip backwards</h5>

          <p>
            Tells iTunes to skip backwards within the current track by a
            predetermined number of seconds. If skipping backwards causes iTunes
            to come to the beginning of the current track then it will not
            attempt to move to the previous track.
          </p>

          <h5>Enable/Disable Track in iTunes</h5>

          <p>
            Tells iTunes to toggle the enabled status of the currently playing
            track in iTunes. This is equivalent to using the mouse to click the
            checkbox that appears next to the track in its playlist. Note that
            there are some tracks for which this hot key will have no effect
            (for example, when listening to Internet Radio).
          </p>

          <h5>Show/Hide iTunes</h5>

          <p>
            If iTunes is not frontmost then pressing this hot key will cause it
            to come to the front. Pressing again will cause it to hide.
          </p>

          <h5>Bring iTunes to Front</h5>

          <p>
            Tells iTunes to come to the front. If it is hidden it will unhide
            and come to the front.
          </p>

          <h5>Launch/Quit iTunes</h5>

          <p>
            Tells iTunes to quit if it is running; if it is not running then
            pressing the hot key launches iTunes.
          </p>

