            If the Synergy Advance Global Menu is present in the menu bar but
            inactive pressing this hot key causes it to pop up and become
            active; you can then navigate the menu using the keyboard, trigger
            the action of the currently highlighted menu item using the Return
            or Enter keys, and cancel menu tracking by pressing the Escape key.
