          <h4>
            <a id="characters" name="characters"></a>Why doesn't WinHex let me
            type the characters I want?
          </h4>

          <p>
            WinHex only allows you to enter characters that are valid for a
            given base. The list below shows the characters which are legal for
            the most common bases:
          </p>

          <table border="0" align="center" cellpadding="10" cellspacing="0">
