          <h4>
            <a id="gray" name="gray"></a>What do the gray question marks mean in
            the ASCII field?
          </h4>

          <p>
            ASCII is a text encoding that uses only 7 bits of information in
            every byte (8 bits). As such, for any given byte there are only 128
            possible numbers for which there exist a corresponding ASCII
            character. For the other 128 possible numbers (those which use the
            eighth bit) there is no printable ASCII representation. In these
            cases, WinHex displays a gray question mark as a placeholder for the
            non-representable value.
          </p>

