          <h4>
            <a id="characters" name="characters"></a>Why doesn't Hextrapolate
            let me type the characters I want?
          </h4>

          <p>
            Hextrapolate only allows you to enter characters that are valid for
            a given base (when typing in a number field) or which can be encoded
            losslessly in the selected text encoding (when typing in a text
            field). The list below shows the characters which are legal for the
            most common bases:
          </p>

