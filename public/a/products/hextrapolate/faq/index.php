          <p>
            One of the reasons Hextrapolate does not support bases greater than
            Base 36 is that this is the highest base that can be represented
            using the 26 letters of the Roman alphabet (a-z) in tandem with the
            numerals 0 through to 9. If we were to introduce a distinction
            between lower and uppercase letters then we could theoretically go
            as high as Base 62.
          </p>

