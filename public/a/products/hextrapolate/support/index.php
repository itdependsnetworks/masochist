          <p>
            The following support resources are available to you 24 hours a day,
            365 days a year through this website.
          </p>

          <h4>Hextrapolate Frequently Asked Questions</h4>

          <p>
            The <a href="../faq/">FAQ</a> contains the answers to the most
            common Hextrapolate questions.
          </p>

          <p class="more">Click <a href="../faq/">here</a> to view the FAQ</p>

          <h4>Support pages</h4>

          <p>
            The
            <a href="https://wincent.com/a/support/"
              >wincent.com support pages</a
            >
            group together all the support resources available to you from this
            website, as well as containing site-wide support information that
            applies not only to Hextrapolate but to all other Wincent products
            and services.
          </p>

          <p class="more">
            Click <a href="https://wincent.com/a/support/">here</a> to go to the
            support pages
          </p>

          <h4>Knowledge base</h4>

          <p>
            The wincent.com
            <a href="https://wincent.com/a/knowledge-base/">Knowledge Base</a>
            is a collection of useful tips, "how to" articles, and general
            information that covers a variety of topics that may be of interest
            to Mac users, developers, and IT.
          </p>

          <p class="more">
            Click <a href="https://wincent.com/a/knowledge-base/">here</a> to
            visit the Knowledge Base
          </p>

          <h4>News pages</h4>

          <p>
            The <a href="https://wincent.com/a/news/">news pages</a> contain the
            latest product and site news, and it's recommended that you check
            for updates before contacting me or opening a support ticket. For
            example, if there is a problem with the site or if I have limited
            access to the Net I will post an update to the news page so that you
            know what's happening.
          </p>

          <p class="more">
            Click <a href="https://wincent.com/a/news/">here</a> to visit the
            news pages
          </p>

          <h4>Contact page</h4>

          <p>
            The
            <a href="https://wincent.com/a/contact/">contact page</a> contains a
            list of useful solutions to common issues (like lost license codes,
            email address updates and so forth). If you wish to provide feedback
            you can use the contact page to get in touch with me. If you want
            personalized technical support, then you will need to open a support
            ticket (see below).
          </p>

          <p class="more">
            Click <a href="https://wincent.com/a/contact/">here</a> to use the
            contact page
          </p>

          <h4>Support tickets</h4>

          <p>
            If the existing online resources aren't sufficient to help you solve
            your problem, please
            <a href="https://wincent.com/a/support/tickets/"
              >open a support ticket</a
            >. This is the fastest way to receive personalized attention,
            because it will not only send me an email but also store the details
            of your inquiry in a database so that I can track the issue more
            easily.
          </p>

          <p class="more">
            Click <a href="https://wincent.com/a/support/tickets/">here</a> to
            open a support ticket
          </p>
