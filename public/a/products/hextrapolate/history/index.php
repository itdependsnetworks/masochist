          <h3>Version history</h3>

          <h4>2.0 (23 April 2007)</h4>

          <ul>
            <li>
              [Enhancement] Added extensive embedded help documentation to the
              application (also available
              <a
                href="https://wincent.com/a/products/hextrapolate/help/front_page.html"
                >online</a
              >).
            </li>
            <li>[Bugfix] Adding missing ellipsis in main menu.</li>
            <li>
              [Note] Added <a href="https://wincent.com/s/kagi/">Kagi</a> as a
              payment option.
            </li>
          </ul>

          <h4>2.0b (10 April 2007)</h4>

          <ul>
            <li>
              [Note] WinHex has now changed its name to
              Hextrapolate. The
              name change has taken place at the request of
              <a href="http://www.x-ways.net/index-m.html"
                >X-Ways Software Technology</a
              >, makers of an existing product for Microsoft Windows which is
              also called "WinHex", has been under development since 1995, and
              has over
              <a href="http://www.x-ways.net/corporate/index-m.html"
                >20,000 registered users</a
              >
              (as of April 2007). To avoid confusion for users of the X-Ways
              software, Wincent has changed the name of its own product to
              Hextrapolate. The X-Ways WinHex website can be found
              <a href="http://www.x-ways.net/winhex/index-m.html">here</a>.
            </li>
            <li>
              [Feature] In addition to the ASCII conversion offered in version
              1.0, the new version adds UTF-8 and UTF-16 (Little Endian and Big
              Endian) options in a pop-up menu.
            </li>
            <li>
              [Feature] Both kinds of pop-ups (text encoding and base) are now
              paired; this provides convenient any-to-any conversion without
              having to toggle settings.
            </li>
            <li>
              [Feature] Byte and bit-count shown in the main window title bar.
            </li>
            <li>
              [Enhancement] Number conversion infrastructure rewritten from
              scratch with a view to better performance.
            </li>
            <li>
              [Enhancement] Hextrapolate is now a Universal Binary that will run
              natively on both Intel and PowerPC processors.
            </li>
            <li>
              [Enhancement] Updated the GMP arbitrary precision arithmetic
              library used by Hextrapolate to version 4.2.1.
            </li>
            <li>
              [Enhancement] Integrated
              WOTest unit testing
              framework.
            </li>
            <li>[Enhancement] Can now enter leading zeros.</li>
            <li>
              [Bugfix] No longer possible to "overflow" the text fields
              (formerly the ASCII field); previously entering a sufficiently
              large quantity of decimal digits could case the ASCII field to
              turn blank.
            </li>
            <li>[Bugfix] Fixed leaking WOMPZWrapper objects.</li>
            <li>
              [Note] The Hextrapolate disk image now shows basic installation
              instructions for a drag-and-drop install.
            </li>
            <li>[Note] Enabled crash reporter functionality.</li>
            <li>
              [Note] Bumped the minimum required Mac OS X version from 10.3 to
              10.4.
            </li>
            <li>
              [Note] Replaced "Registration feed paid" checkbox with a proper
              interface showing registered user email address and buttons for
              purchasing, registering, unregistering and recovering lost license
              codes. Codes have already been sent to existing registered users;
              please contact me (<a href="mailto:support@wincent.com"
                >support@wincent.com</a
              >) if you have not received your code.
            </li>
          </ul>

          <h4>1.0b2 (21 January 2003)</h4>

