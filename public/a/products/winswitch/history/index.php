          <h3>Version history</h3>

          <h4><a id="3.2.1" name="3.2.1"></a>Version 3.2.1 (4 April 2006):</h4>

          <ul>
            <li>
              Remove the spurious crash warning that may be shown on first
              launch.
            </li>
          </ul>

          <h4><a id="3.2" name="3.2"></a>Version 3.2 (3 April 2006):</h4>

          <ul>
            <li>French localization of the installer by Ronald Leroux.</li>
            <li>
              "Log out&hellip;" menu item visible when holding down the option
              key (<a
                href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=332"
                >feature request #332</a
              >).
            </li>
            <li>
              Upgraded embedded WODebug framework to version 1.4 which is
              capable of providing much more detailed crash reports.
            </li>
          </ul>

          <h4><a id="3.2b" name="3.2b"></a>Version 3.2b (10 March 2006):</h4>

          <ul>
            <li>
              WinSwitch is now a Universal Binary which runs natively on both
              PowerPC and Intel processors; in addition to the WinSwitch Menu
              Extra itself all the supporting applications, tools and bundles
              are also Universal Binaries (this includes the WinSwitchHelper
              background application, the menu-extra-tool, the MenuCracker Menu
              Extra, and of course the installer).
            </li>
            <li>
              WOHotKey framework updated; user visible improvements include the
              addition of tool tips to the editing field when setting hot key
              combinations and the elimination of console warning message when
              clicking to the right of defined hot keys in an active hot key
              editing view.
            </li>
            <li>
              Corrected rare problem in which some user pictures would appear
              shrunken inside the menu (<a
                href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=137"
                >bug #137</a
              >); to see this fix you must upgrade to Tiger (Mac OS X 10.4).
            </li>
            <li>
              Made changes to ensure that the vertical spacing of icons in the
              menu exactly matches that used by Apple's version of the Fast User
              Switching menu (<a
                href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=136"
                >bug #136</a
              >); to see this fix you must upgrade to Tiger (Mac OS X 10.4).
            </li>
            <li>
              Now ships with the
              WOTest unit testing
              framework so that users can run the test suite locally.
            </li>
            <li>
              Minor refinements to the Spanish localization (now refers to
              "nombre de pila" rather than just "nombre").
            </li>
            <li>Corrections to Danish localization.</li>
            <li>
              Additions to Japanese localization, provided by Yuzuru Shiraiwa.
            </li>
          </ul>

          <h4><a id="3.1" name="3.1"></a>Version 3.1 (25 April 2005):</h4>

          <ul>
            <li>
              Compatible with Mac OS X 10.4 Tiger (fixes
              <a href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=139"
                >bug #139</a
              >).
            </li>
            <li>
              Corrections and a large number of additions by Charly Avital to
              the Hebrew localization.
            </li>
            <li>
              Numerous layout adjustments in the non-English localizations to
              expose previously clipped text.
            </li>
            <li>WOBezel framework updated to version 1.2.</li>
            <li>WOHotKey framework updated to version 1.2.</li>
            <li>WODebug framework updated to version 1.2.</li>
            <li>
              Built using GCC 4.0, which should result in better optimized code.
            </li>
          </ul>

          <h4><a id="3.0.1" name="3.0.1"></a>Version 3.0.1 (29 March 2005):</h4>

          <ul>
            <li>
              Corrections to Japanese localization by Yuzuru Shiraiwa (textual
              and layout changes).
            </li>
            <li>
              Corrections to French localization by Fabrice Laurens (many
              translated strings were clipped in the preferences window).
            </li>
            <li>
              Partial Hebrew localization by Seth Dimbert, Doron Neuberger and
              Yehoshua Goldring (Hyman Brand Hebrew Academy).
            </li>
          </ul>

          <h4><a id="3.0" name="3.0"></a>Version 3.0 (9 March 2005):</h4>

          <ul>
            <li>
              Additional checking on selecting "Preferences" menu item to ensure
              that the WinSwitchHelper background application is running.
            </li>
            <li>
              For increased security, additional checking on Switch-In Items and
              Switch-Out Items prior to launching. Items will only be launched
              if both they and the containing folder are owned by the current
              user and the permissions are set so that only the current user has
              write privileges. If these conditions are not met, WinSwitch logs
              a warning to the console. (Note that by default, Mac OS X does
              create new folders and items with appropriate permissions.)
            </li>
            <li>
              Fixed bug where user picture size in the menu would not appear at
              the default size when first run.
            </li>
            <li>
              Fixed bug where the option to include the root user in the menu
              was inverted (in other words, turning the option off actually
              turned it on and vice versa).
            </li>
            <li>
              Eliminated harmless console warning at startup, "Could not find
              image named `bezelClose'".
            </li>
            <li>
              Fixed crash that would occur on systems with faulty accounts in
              the NetInfo database (<a
                href="https://typechecked.net/a/support/bugs/show_bug.cgi?id=68"
                >bug #68</a
              >).
            </li>
            <li>Corrected alignment error in preferences window.</li>
            <li>New Turkish localization by Alphan Gunaydin.</li>
            <li>Updates to the Japanese localization by Yuzuru Shiraiwa.</li>
