          <h4><a id="1.2" name="1.2"></a>Version 1.2 (5 April 2004):</h4>

          <ul>
            <li>
              Major overhaul of underlying mechanism used to get information
              about users: now uses the NetInfo API to send queries directly to
              NetInfo instead of the previously-used POSIX layer calls.
            </li>
            <li>
              This means that users in a lab/network context can use WinSwitch
              and will see only the local user accounts on their machine instead
              of all the users across the network.
            </li>
            <li>
              Exclusion of non-local users makes WinSwitch more efficient.
            </li>
            <li>
              Grabbing custom user picture is also more efficent (no longer need
              NSTask/nicl).
            </li>
            <li>
              Use of NetInfo now means that WinSwitch updates instantly when a
              full username is edited in the Accounts pane of the System
              Preferences (previously WinSwitch waited two seconds before
              updating in order to give the POSIX layer time synchronize with
              the change).
            </li>
            <li>
              Removal of POSIX calls also cures the occasional &quot;double
              listing&quot; problem that some users saw after adding, deleting
              or changing users in the Accounts preference panel.
            </li>
            <li>
              Installer now uses less stringent error checking; it appears that
              a conflict with an Unsanity product was producing an error code
              even though WinSwitch was successfully added to the menu bar.
            </li>
            <li>French localization by Fabrice Laurens.</li>
          </ul>

