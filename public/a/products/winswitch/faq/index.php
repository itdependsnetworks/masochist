          <h4>
            <a name="switchInSwitchOut" id="switchInSwitchOut"></a>What is the
            purpose of the &quot;Switch-In Items&quot; and &quot;Switch-Out
            Items&quot; folders?
          </h4>

          <p>
            WinSwitch will launch any items (or aliases to items) placed in the
            &quot;~/Library/Application Support/WinSwitch/Switch-In Items/&quot;
            folder whenever you switch to another user account.
          </p>

          <p>
            Similarly, it will launch items or aliases to items placed in the
            &quot;~/Library/Application Support/WinSwitch/Switch-Out
            Items/&quot; folder whenever you switch back from another user.
          </p>

          <p>
            You may choose to launch applications, documents, AppleScripts, or
            any other item that you would double-click in the Finder. For
            security, items will only be launched if both they and the
            containing folder are owned by the current user and the permissions
            are set so that only the current user has write privileges (if these
            conditions are not met, WinSwitch logs a warning to the console).
            Note that by default, Mac OS X does create new folders and items
            with appropriate permissions.
          </p>

          <p>
            The WinSwitch installer creates a folder called
            &quot;/Library/Application Support/WinSwitch/Sample Switch
            Items/&quot; containing a number of sample AppleScripts that
            demonstrate the utility of this feature.
          </p>

          <p>
            To try out the sample scripts, drag, copy or make an alias in the
            appropriate folder. Note that the sample scripts are installed
            globally (for all users) in the &quot;/Library/&quot; hierarchy, but
            that to use them you must install them locally (for the current user
            only) in the &quot;~/Library/&quot; hierarchy in your home folder.
          </p>

          <p>
            For a more complicated example, see
            <a
              href="http://www.macosxhints.com/article.php?story=20041104142244497"
              >this hint</a
            >
            that a user has posted over at
            <a href="http://www.macosxhints.com/">Mac OS X Hints</a> about using
            <a href="https://wincent.com/a/products/winswitch/">WinSwitch</a> to
            tame <a href="http://www.logitech.com/">Logitech's</a> Control
            Center software; or
            <a
              href="http://www.macosxhints.com/article.php?story=20061212090013611"
              >this hint</a
            >, about how WinSwitch can help sharing iTunes or iPhoto libraries
            across multiple accounts.
          </p>

