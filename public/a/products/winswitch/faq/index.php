          <h4>
            <a name="source" id="source"></a>Why isn't the source for the
            WinSwitchHelper background application included in the source
            archive?
          </h4>

          <p>
            The source code for producing the WinSwitch.menu bundle is, and
            since the beginning has been, available under the BSD license. The
            WinSwitchHelper background application contains confidential,
            proprietary code and so it cannot be included in the download
            archive. You can still build a functional copy of WinSwitch based on
            the open source segment of the project. The WinSwitchHelper binary
            is included in the archive, so you can build a copy identical to the
            version made available for download from this web site.
          </p>

          <p>
            I don't anticipate that this will slow down the WinSwitch project,
            because since it's release near the start of 2004 there have been
            many thousands of downloads of the project source code, yet not a
            single person has contributed a line of code or a bug fix. I chose
            the BSD license because its predecessor, FUSe, was BSD licensed. It
            does appear, however, that for a project of this size, the open
            source model doesn't bring any benefit to the project. I do have
            plans for experimenting with other models of open source in the
            future, trying to find something that works, but it will most likely
            be something other than a BSD (or even a GPL) license.
          </p>

