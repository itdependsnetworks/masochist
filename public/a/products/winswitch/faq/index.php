          <h4><a name="root" id="root"></a>How do I activate the root user?</h4>

          <p>
            To include the root user in WinSwitch's list of users, go to the
            &quot;Show&quot; submenu and toggle the &quot;Root user&quot; menu
            item. By default, the root user on Mac OS X has a full name of
            &quot;System Administrator&quot;, and this is how it appears in the
            list. You can toggle this setting back and forth to your liking and
            WinSwitch will remember your preference.
          </p>

          <p>
            In order to actually log in or switch to the root account, you first
            need to activate it and set an appropriate password. You can do this
            by using Apple's &quot;NetInfo Manager&quot; application, stored in
            the &quot;Utilities&quot; folder of your &quot;Applications&quot;
            folder. In NetInfo Manager, choose &quot;Activate root user&quot;
            from the &quot;Security&quot; menu. You should also use the
            &quot;Change Root Password&quot; menu item to select an appropriate
            password.
          </p>

          <p>
            You should take care when running as the root user; the root user
            has the power to damage the system if not used carefully (for
            example, by deleting critical system files). Apple leaves the root
            user disabled by default for security reasons. In general most
            operations can be performed from a non-root account that has
            administrator privileges.
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a name="tweak" id="tweak"></a>How do I adjust the size of my user
            picture in the menu bar?
          </h4>

          <p>
            The short answer is to upgrade to WinSwitch version 3.0b or above.
          </p>

          <p>
            For older versions, by default, WinSwitch displays your user picture
            at a size of 19 by 19 pixels. If you wish to set it to a smaller
            size (for example, 16 by 16), enter the following command in the
            Terminal (all on one line):
          </p>

          <pre>
defaults write com.wincent.WinSwitch 'User picture size' -float 16.0</pre
          >

          <p>To set it back to 19 by 19 you would use:</p>

          <pre>
defaults write com.wincent.WinSwitch 'User picture size' -float 19.0</pre
          >

          <p>
            Changes take effect the next to you log in or restart your computer.
            (If you're impatient to see the result you can kill the
            SystemUIServer by issuing a &quot;killall SystemUIServer&quot;
            command in the Terminal).
          </p>

          <p>
            WinSwitch will remember your settings. Values less than 5.0 or
            greater than 19.0 will be ignored.
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a name="button" id="button"></a>How do I get the &quot;Switch
            users&quot; button to appear in the screensaver password dialog?
          </h4>

          <p>
            When Apple's Fast User Switching menu is turned on (by checking the
            appropriate checkbox in the Accounts preferences pane), the
            behaviour of the screensaver password dialog changes. An additional
            button, &quot;Switch users&quot;, appears in the dialog. It also
            appears in the dialog that you see if you have your computer set to
            ask for a password when woken from sleep.
          </p>

          <p>
            WinSwitch doesn't attempt to meddle with the internals of the
            screensaver or the Accounts preference pane, so it has no way of
            turning this feature on or off.
          </p>

          <p>
            You can, however, trick the system into displaying the button. The
            &quot;trick&quot; consists of turning Apple's Fast User Switching
            off and on again, then quitting the System Preferences. Their menu
            will be reinserted into the menu bar when you do this (alongside
            WinSwitch), so hold down the Command key and drag the Apple version
            out of the menu bar, leaving WinSwitch in place. From that point
            onwards the screensaver and wake-from-sleep password dialogs will
            show the button.
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a name="comeBack" id="comeBack"></a>Why does Apple's Fast User
            Switching menu come back when I turn on switching in the Accounts
            preference pane?
          </h4>

          <p>
            WinSwitch adopts a conservative policy with respect to replacing
            Apple's Fast User Switching menu. When you install WinSwitch you are
            not overwriting Apple's menu; you are merely installing an
            alternative which happens to work much better.
          </p>

          <p>
            The Accounts preference pane, however, continues to be hard-wired to
            work with Apple's version of the menu. So if you install WinSwitch
            and then turn on switching in the Accounts preference pane, then
            Apple's menu will be reinsterted into the menu bar and you will see
            two Fast User Switching menus!
          </p>

          <p>
            The solution is to simply hold down the Command key and drag the
            unwanted version out of the menu bar.
          </p>

          <p>
            WinSwitch doesn't make any attempts to meddle with the internals of
            the Accounts preference pane to prevent this from happening, as that
            would probably be a bad thing. As it stands, WinSwitch is nicely
            self-contained inside the WinSwitch.menu bundle. It doesn't spray
            files all over your system; it just installs itself inside
            &quot;/Library/Menu Extras&quot;, and it is easy to remove.
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a name="zombie" id="zombie"></a>Why doesn't WinSwitch go away when
            I uncheck the Fast User Switching checkbox in the Accounts
            preference pane?
          </h4>

          <p>
            Apple's Accounts preference pane is hard-wired to communicate with
            their own Fast User Switching menu. WinSwitch doesn't attempt to
            overwrite that menu nor pose as it by using the same name and bundle
            identifier.
          </p>

          <p>
            As such, when you uncheck the checkbox, the Accounts preference pane
            tries to remove the Apple version of the menu rather than WinSwitch.
          </p>

          <p>
            You can easily remove WinSwitch by holding down the Command key and
            dragging it out of the menu bar. Running the installer and clicking
            the &quot;Uninstall&quot; button would have the same effect.
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a name="accidentallyDragged" id="accidentallyDragged"></a>I
            accidentally dragged WinSwitch out of the menu bar; how do I get it
            back?
          </h4>

          <p>
            As already stated, WinSwitch doesn't tamper with the internals of
            the Accounts preference pane, so if you try to turn it on by
            activating the Fast User Switching checkbox you'll find that the
            Apple version comes back instead of WinSwitch.
          </p>

          <p>
            Because WinSwitch is self-contained and doesn't install any hooks
            into the operating system, you can put it back in the menu bar
            simply by double-clicking it. The WinSwitch.menu bundle is installed
            in &quot;/Library/Menu Extras&quot;. Just double-click it, then hold
            down the Command key and drag it to your preferred position in the
            menu bar if necessary.
          </p>

          <p>
            This is essentially what's happening when you run the &quot;Activate
            WinSwitch&quot; script that gets installed into
            &quot;/Users/Shared&quot;. It saves uses the trouble of navigating
            into the somewhat complicated Library folder to find the bundle.
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a name="restore" id="restore"></a>How do I get the Apple version of
            the Fast User switching menu back if I uninstall WinSwitch?
          </h4>

          <p>
            Apple's menu is a bundle just like WinSwitch, so you can restore it
            simply by double-clicking it. You can find it on your disk in the
            &quot;/System/Library/CoreServices/Menu Extras/&quot; folder. It's
            labelled &quot;User.menu&quot;.
          </p>

          <p>
            Double-click it, then hold down the Command key and drag it to your
            preferred position in the menu bar if necessary.
          </p>

          <p class="more">[<a href="#top">Back to top...</a>]</p>

          <h4>
            <a name="localize" id="localize"></a>How do I localize WinSwitch
            into my own language?
          </h4>

          <p>
            Localizing WinSwitch is very easy because it has such a small number
            of user-visible strings. Localization can be performed in less than
            five minutes by a native-language speaker who understands the basics
            of the process.
          </p>

          <p>
            Start by downloading the localization pack from the
            <a href="../download/">downloads list</a> on the main WinSwitch
            page.
          </p>

          <p>
            Then, edit the two text files using a UTF-16 capable text editor
            such as
            <a href="http://developer.apple.com/intl/localization/tools.html"
              >Apple's free ADViewer</a
            >. (ADViewer is the tool
            <a href="http://developer.apple.com/intl/localization/process.html"
              >recommended by Apple</a
            >
            for localization purposes.)
          </p>

          <p>
            In each case you see a comment followed by the original English
            version on the left hand side; you must replace the version on the
            right hand side with the translated text. Here's an example from the
            Spanish version of WinSwitch; the original English version:
          </p>

          <pre>
/* Show */
&quot;Show&quot; = &quot;Show&quot;;</pre
          >

          <p>Is edited so that it looks like this:</p>

          <pre>
/* Show */
&quot;Show&quot; = &quot;Mostrar&quot;;</pre
          >

          <p>
            Once you've edited the files, you change the name of the
            &quot;English.lproj&quot; folder appropriately (for example, to
            &quot;Spanish.lproj&quot;), zip them up and email them to me (to
            "win" at this domain) for inclusion in the next release of
            WinSwitch. Be sure to let me know your name and any other details
            (for example an email address and/or website link) that you'd like
            included in the WinSwitch <a href="../credits/">credits</a>.
          </p>

          <p>
            If you are interested, you can read more about localizing
            applications on Mac OS X
            <a href="http://developer.apple.com/intl/localization/">here</a>.
          </p>

