          <h3>
            Fast User Switching without wasting valuable space in your Menu Bar
          </h3>

          <img
            style="float:left; margin: 20px; "
            src="../gfx/winswitch-icon.jpg"
            alt="WinSwitch icon"
            width="128"
            height="128"
          />
          <p>
            Apple did a superb job with its new Fast User Switching feature in
            Panther (Mac OS X 10.3), but for users with long names, the
            associated menu item is a terrible waste of precious Menu Bar real
            estate. WinSwitch fixes that and brings a number of other powerful
            features that are not available on either Panther or Tiger (Mac OS X
            10.4).
          </p>

          <img
            src="https://typechecked.net/a/products/winswitch/screenshots/tiny-preview.gif"
            width="57"
            height="44"
            hspace="5"
            vspace="5"
            align="right"
            alt=""
          />
          <p>
            Wouldn't it be nice if you could access the feature through a
            compact, icon-sized Menu Bar icon? WinSwitch does exactly that,
            closely matching the performance and style of the Apple-provided
            menu, but without all the bulk. In fact, it even corrects some
            problems with Apple's menu by eliminating certain &quot;daemon&quot;
            users which shouldn't show up in the list, and by improving the
            alignment of user icons in the menu. WinSwitch gives you rapid
            access to the Accounts preference pane, and is completely
            customizable, allowing you to choose from six different menu bar
            styles:
          </p>

