          <h3>Wincent Strings Utility</h3>

          <p>
            The Wincent Strings Utility is a localization tool derived from
            stringsUtil by
            <a href="http://www.omnigroup.com/">Omni Development</a>. It is a
            command line tool designed to be called from within Xcode shell
            script build phases. In its first mode of operation it parses
            localizable strings files in the development language and transforms
            them for use with existing localized variants; the three possible
            transformations are merge, extract and combine (for incremental
            localization).
          </p>

          <p>
            In its second mode of operation it can be used to preprocess
            localized InfoPlist.strings files by scanning them for Info.plist
            keys and performing substitutions of values such as copyright year
            and version number. For more information see
            <a href="wincent-strings-util.1.txt">this plaintext copy</a> of the
            <tt>wincent-stings-util</tt> man page.
          </p>

