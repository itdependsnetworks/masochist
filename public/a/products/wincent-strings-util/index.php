          <h4><a id="v1.2" name="v1.2"></a>1.2 (6 January 2008)</h4>

          <ul>
            <li>
              New <tt>--extract</tt> and <tt>--combine</tt> switches for even
              easier incremental localization.
            </li>
            <li>
              Support recursive substitution when performing preprocessing on
              <tt>InfoPlist.strings</tt> files.
            </li>
            <li>
              Use double-hyphens for all switches, instead of the less-orthodox
              single hyphens previously used.
            </li>
            <li>
              Add checks for duplicate keys within the same file (this is never
              correct so the user should be notified about it).
            </li>
            <li>
              Stricter parsing of input files when using the
              <tt>--info</tt> switch; this allows us to detect malformed input
              files as well guess the input encoding for files which do not have
              an explicit BOM (Byte Order Mark).
            </li>
            <li>
              Allow explicit override of output encoding using the new
              <tt>--encode</tt> switch.
            </li>
            <li>Always include an explicit BOM in the output file.</li>
            <li>Add installer package for use with Apple's installer.</li>
            <li>
              Convert to Objective-C 2.0 and Garbage Collection; as such this
              release requires Mac OS X Leopard.
            </li>
            <li>
              Migrate source code from Subversion to Git and make corresponding
              adjustments such as removing the embedded $Id$ number from the
              built executable.
            </li>
            <li>
              Update build process to use
              <a href="https://wincent.com/a/products/buildtools/"
                >Wincent Build Tools</a
              >.
            </li>
            <li>
              Add automated test suite using
              <a href="http://rspec.info/">RSpec</a>.
            </li>
          </ul>

          <p>
            A code-level changelog for this release is available
            <a href="history/1.2-release-notes.txt">here</a>.
          </p>

          <h4><a id="v1.1" name="v1.1"></a>1.1 (17 August 2006)</h4>

