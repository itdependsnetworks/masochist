          <h2>October 26, 2007</h2>

          <h3 id="a002846">Leopard product readiness</h3>

          <p>
            With
            <a href="https://typechecked.net/wiki/Leopard">Leopard</a>
            only 15 hours from release it's time for a product status report:
          </p>
          <a name="Leopard-ready"></a>
          <h3>Leopard-ready</h3>

          <p>
            The following products have received extensive testing on
            pre-release versions of Leopard and are expected to work with a high
            degree of compatibility. When Apple ships the final version to
            developers further testing will be performed to see if any further
            changes are required:
          </p>
          <ul>
            <li>
              <a href="http://typechecked.net">Synergy</a>: the lightweight,
              robust, and feature-packed iTunes controller.
            </li>
            <li>
              Install: simple and
              dependable installation solution for developers.
            </li>
            <li>
              WOTest: open source
              Objective-C unit testing framework.
            </li>
            <li>
              Wincent Strings Utility:
              open source localization helper tool for developers.
            </li>
          </ul>
          <a name="Beta-status"></a>
          <h3>Beta-status</h3>

          <p>
            This product hasn't been heavily tested with Leopard but conflicting
            reports have been received that it continues to work under the new
            operating system; others report that they've been unable to install
            it. When the Leopard final arrives further investigation will be
            undertaken.
          </p>
          <ul>
            <li>
              WinSwitch: Fast User
              Switching menu replacement.
            </li>
          </ul>
          <a name="Coming%20soon"></a>
          <h3>Coming soon</h3>

          <p>
            These products aren't yet Leopard-ready but work is well on the way
            to completion (about 90% done) and so I hope to release
            Leopard-compatible versions within the next two to three weeks:
          </p>
          <ul>
            <li>
              Synergy Advance: Plug-in
              based audio companion.
            </li>
            <li>
              Hextrapolate:
              Programmers number conversion tool.
            </li>
          </ul>
          <a name="All%20other%20products"></a>
          <h3>All other products</h3>

          <p>
            There are a number of other products which aren't specifically tied
            to the Mac OS X platform, and so should work on Leopard just as they
            did under Tiger:
          </p>
          <ul>
            <li>
              Bansshee: protection
              against SSH dictionary attacks.
            </li>
            <li>
              Walrus: object-oriented
              templating system written in
              <a href="https://typechecked.net/wiki/Ruby">Ruby</a>.
