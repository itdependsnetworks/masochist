          <h3>About Wincent Colaiuta</h3>

          <p>
            Wincent Colaiuta is a software developer, Mac commentator and
            recognised authority on Mac OS X. He maintains a web presence at
            wincent.com, the official Wincent software site which now
            incorporates what was previously known as wincent.org, a Mac-centric
            weblog and information site. Wincent's personal blog can be found
            <a href="https://wincent.com/a/about/wincent/weblog/">here</a>.
          </p>

          <p>
            Wincent became well known in the world of the Mac web in 2001 in the
            lead-up to the release of Mac OS X 10.0 due to his infamous series
            of pre-release expos&eacute;s of the highly anticipated operating
            system. At its peak, the wincent.org website was attracting more
            than 50,000 page views per day.
          </p>

          <p>
            He has written for
            <a href="http://hotwired.lycos.com/webmonkey/01/16/index3a.html"
              >Lycos</a
            >
            and been a regular columnist for the
            <a href="http://www.macobserver.com/">Macobserver</a>, where he
            co-wrote the
            <a href="http://www.macobserver.com/tips/hotcocoa/">Hot Cocoa</a>
            column during 2001. His work has been
            <a href="http://www.hotwired.co.jp/webmonkey/2001/30/index3a.html"
              >translated into Japanese</a
            >.
          </p>

          <h4>Wincent and the Mac</h4>

          <p>
            Wincent has been a Mac user since 1996 and a computer user since his
            childhood in the early 80s.
          </p>

          <p>
            He sees himself as a Macintosh user and advocate for far into the
            forseeable future. He believes that the wedding between the power of
            Unix and the user experience of the Mac is a winning formula that
            firmly places Mac OS X as the world's best desktop operating system.
          </p>

          <h4>Other biographical information</h4>

          <p>
            Wincent is an Australian citizen, although he currently lives in
            Spain. He is a fluent speaker of English and Spanish.
          </p>

          <h4>Is Wincent related to the famous drummer, Vinnie Colaiuta?</h4>

          <p>No. There is no relation.</p>

