        <h2>November 13, 2005</h2>

        <h3 id="a000146">An Xcode feature request</h3>

        <p>
          Xcode has toolbar buttons for "Clean", "Clean All", "Build", "Build
          and Run" and so forth. Many, many times each day I find myself
          pressing "Clean" and then "Build" immediately afterwards. It would be
          a nice convenience and time saver if there was a "Clean and Build"
          button that combined the two operations, or failing that, if holding
          the Option key down while pressing the "Build" or "Build and Run" (or
          even the "Build and Debug") buttons could modify their behaviour to
          execute a clean before proceeding with the build.
        </p>
        <p>
          <a
            href="https://wincent.dev/a/about/wincent/weblog/archives/2005/11/an_xcode_featur.php"
