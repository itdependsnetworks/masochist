        <h2>March 16, 2006</h2>

        <h3 id="a000249">Frustration</h3>

        <p>
          Have just spent about six hours trying to track down a bug in my code
          that was causing a crash in Synergy Advance. Turns out that it wasn't
          a bug in my code at all, rather it was a misunderstanding of the way
          NSConnection works.
        </p>
