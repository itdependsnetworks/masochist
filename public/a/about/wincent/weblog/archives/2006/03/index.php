        <h3 id="a000259">Involuntary Reboot Log: #38</h3>

        <p>
          Lesson of the day, never double-click the application you can find at
          <tt
            >/Developer/Applications/Performance Tools/CHUD/Hardware
            Tools/Reggie SE</tt
          >. I just made that mistake, out of curiosity, and it hard froze my
          system. By "hard freeze" I mean that not even the mouse pointer would
          move, the clock stopped updated, the machine did not respond to pings
          from elsewhere on the local network, and the only thing that seemed to
          be working well were the fans (roaring like hair dryers).
