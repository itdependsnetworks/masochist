          I define in my code:#define WO_YES [NSNumber
          numberWithBool:YES]#define WO_NO [NSNumber numberWithBool:NO]This
          enables me to use NSNumbers just as easily as I would use simple
          booleans:variable1 = YES; // easyvariable2 = WO_YES; // just as
          easyWhen is a BOOL better than an NSNumber?When you care about the
          overhead introduced by using a fully-fledged object instead of a
          simple BOOL.
