        <h2>May 21, 2006</h2>

        <h3 id="a000327">Involuntary Reboot Log #39</h3>

        <p>
          Witness as Adium starts to beachball before coming to the front.Notice
          that NetMonitor continues to update in the Dock, but that the network
          traffic has stalled, indicating that the hidden Azureus application
          has effictively stopped working.Notice that the CPU graph provided by
          the Activity Monitor in the graph, which up until now has shown that
          both 2.5GHz 64-bit CPUs have been idling away at about 1 or 2%
          utilization throughout this entire fiasco (although the machine's fans
          continued to roar at their usual hair-dryer volume the whole time),
          has stopped updating.Notice that the only window which doesn't show a
          beachball when you mouse over it is the one belong to Mail.app....
          Decide that just to be safe you'll follow your standard policy of
          booting from optical media and running Disk First Aid, seeing as
          neither Apple's journaling file system nor their software RAID
          implementation have proven to be adequate protection in the
          past.Insert Tiger install disc.
