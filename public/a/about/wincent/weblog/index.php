          <h2>February 2, 2008</h2>

          <h3 id="a003874">Ragel wins! Fatality!</h3>

          <p>
            One of my projects is a
            fast wikitext-to-HTML translator. It's a
            <a href="https://wincent.com/knowledge-base/Ruby">Ruby</a> extension
            written in <a href="https://wincent.com/knowledge-base/C">C</a> with
            speed being one of its top design goals (robustness, predictability
            and security being the others). I'm working on this because the
            <a href="https://wincent.com/knowledge-base/Rails">Rails</a> rewrite
            of this site will use
            <a href="https://wincent.com/knowledge-base/wikitext%20markup"
              >wikitext markup</a
            >
            for basically everything. Rails has a bad reputation for being slow
            and resource hungry, and that's why one of my primary objectives is
            speed; I don't want my wikitext parser being a bottleneck.
          </p>
          <p>
            The translator itself consists of a
            hand-coded parser written in C
            paired with a scanner/tokenizer/lexer generated from a
            grammar specification.
          </p>
          <p>
            Up until now the scanner used a
            <a href="https://wincent.com/knowledge-base/ANTLR">ANTLR</a
            >-generated lexer using the &quot;C target&quot; (although ANTLR
            itself is written in Java, it can target multiple languages,
            including C). The C target is the current speed king among ANTLR
            targets and is likely to remain so for the foreseeable future. It
            too is engineered with speed in mind and is quite frugal with
            resources (for example, when emitting tokens it maintains pointers
            into the input stream rather than making a copy of the substring for
            each token).
          </p>
          <p>
            And the results were quite snappy. But for some time I've had the
            sneaking suspicion that a
            <a href="https://wincent.com/knowledge-base/Ragel">Ragel</a
            >-generated scanner would be faster. I suspected this even though
            ANTLR uses all sorts of clever tricks like trying to predict which
            path to take and only backtracking if prediction fails.
          </p>
          <p>
            You see, Ragel generates simple state machines: the building blocks
            of regular expressions. Whereas ANTLR is like a complex and
            unpredictable magic clock, Ragel state machines are totally
            transparent and predictable. I've spent many months trying to use
            ANTLR on a number of different projects and in every case
            <em>except</em> for this wikitext translator I've ended up putting
            it aside because I couldn't divine the incantations required to make
            it do its black magic. Ragel, on the other hand, is absurdly simple.
            You can build complex things out of it, but the base components are
            easily comprehensible. Compared to ANTLR, Ragel is incredibly easy
            to learn.
          </p>

