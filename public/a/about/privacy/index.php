          <h3>Privacy policy</h3>

          <h4>In a nutshell...</h4>

          <p>
            Wincent cares about and respects your privacy. Wincent will only
            collect personal information if you voluntarily offer it, and
            Wincent will never sell or otherwise disclose that information to a
            third party.
          </p>

          <h4>Website visitors</h4>

          <p>
            Wincent does not collect any data from website visitors, other than
            the usual information which is logged by the Apache webserver when a
            remote client connects (such as IP address, status code and user
            agent). None of this is information that could personally identify a
            visitor. Server logs are periodically purged so that only the most
            recent information remains on the server.
          </p>

          <p>
            Standard security measures are in place so that any information that
            you voluntarily provide (for example, if you create an account for
            the <a href="https://wincent.dev/a/support/forums/">forums</a> or
            the
            <a href="https://wincent.dev/a/support/bugs/"
              >bug tracking and feature requests database</a
            >) is best protected against loss, misuse, disclosure to or
            alteration by third parties. Although Wincent makes a best effort to
            protect your confidentiality, it can offer no guarantees in the
            event of a security breach.
          </p>

          <h4>Customer records</h4>

          <p>
            Wincent maintains a private list of registered users based on the
            information provided by the payment processing services of
            <a href="http://www.paypal.com/">PayPal</a> and
            <a href="http://www.kagi.com/">Kagi</a>. This information does not
            include credit card or banking details, and it is not stored on this
            server or any other that is accessible to the public.
          </p>

          <h4>"Phoning home"</h4>

          <p>
            Wincent products do not "phone home" or transmit your personal
            information across the network. Some products offer an auto-update
            feature in which they periodically connect to the Wincent webserver
            (specifically, wincent.dev) to find out if a newer version is
            available. In the process, no information is transmitted about you
            or your computer; the product merely asks the Wincent server what is
            the latest version, and offers the user an opportunity to download
            it if it is newer. The auto-updates feature is optional and can be
            deactivated.
          </p>

          <h4>Product activation</h4>

          <p>
            Some Wincent products are equipped with product activation. In
            product activation the software connects once (and only once) to the
            wincent.dev server in order to validate the software's license. None
            of your personal information is sent to the server in this process
            (not even your license code is sent). The product activation system
            has been carefully engineered over a period of nearly two years to
            ensure that your privacy is fully protected and that the process is
            convienent. More detailed information is available in the
            <a href="https://wincent.dev/a/support/activation/privacy/"
              >Product Activation Privacy FAQ</a
            >.
          </p>
